package backjoon.`20260227`

class Solution9 {
    fun solution(score: Array<IntArray>): IntArray {
        // 학생별 총점(영어+수학)
        val 총점목록 = mutableListOf<Int>()
        for (학생점수 in score) {
            val 영어 = 학생점수[0]
            val 수학 = 학생점수[1]
            총점목록.add(영어 + 수학)
        }

        // 총점들을 내림차순으로 정렬한 기준표(등수용)
        val 등수기준표 = 총점목록.toMutableList()
        등수기준표.sortDescending()

        // 각 학생의 등수 구하기
        val 등수결과 = IntArray(score.size)
        for (학생번호 in 총점목록.indices) {
            val 내총점 = 총점목록[학생번호]
            val 등수 = 등수기준표.indexOf(내총점) + 1
            등수결과[학생번호] = 등수
        }

        return 등수결과
    }
}



/**
내총점 = 총점목록[학생번호]
“이 학생의 총점은 얼마야?”
예: 0번 학생 총점이 150이면 내총점 = 150

등수 = 등수기준표.indexOf(내총점) + 1
 **정렬된 기준표에서 내 총점이 “처음 나타나는 위치”**를 찾는 거야.

예를 들어 예시1에서
총점목록 = [150, 140, 110, 130]
등수기준표(정렬) = [150, 140, 130, 110]

내총점이 150이면
indexOf(150) = 0 (맨 앞) → +1 해서 1등

내총점이 140이면
indexOf(140) = 1 → 2등

내총점이 130이면
indexOf(130) = 2 → 3등

내총점이 110이면
indexOf(110) = 3 → 4등

등수결과[학생번호] = 등수
“정답 칸에 이 학생 등수를 적어놓자”

동점이 왜 자동으로 같은 등수가 되냐?

예를 들어 총점이 [75, 75, 40, 95, 95, 100, 20]이면
정렬 기준표는 [100, 95, 95, 75, 75, 40, 20]

95의 indexOf(95)는 항상 1 (처음 등장 위치)
그래서 95인 애들은 둘 다 1+1=2등

75의 indexOf(75)는 항상 3
그래서 둘 다 3+1=4등
→ 공동등수 규칙이 딱 맞게 나와.

