/*
[문제 요약]
- 채널 목록이 주어졌을 때, 리모컨 버튼 조작으로
  KBS1을 0번 인덱스, KBS2를 1번 인덱스로 옮겨야 한다.
- 이 풀이에서 실제로 쓰는 버튼:
  "1" = 커서를 아래로 한 칸 이동
  "4" = 현재 채널을 위 채널과 swap (채널을 한 칸 위로 올림)
- 출력: 버튼 입력 문자열

[핵심 아이디어]
- "대상 채널 위치까지 커서를 내린 뒤", "대상 채널을 목표 위치까지 위로 끌어올리는"
  2단계 패턴을 함수로 만든다.
- 이 패턴을 KBS1, KBS2에 순서대로 적용하면 된다.

[데이터 구조]
- 채널들: 현재 채널 순서
- 커서: 현재 선택 위치
- sb: 버튼 입력 기록

[처리 순서]
<1> 채널 목록을 읽고 커서를 0번에 둔다.
<2> 이동(대상, 목표) 함수를 이용해 대상 채널을 목표 인덱스로 끌어올린다.
    - 커서를 대상 위치까지 내리며 "1" 기록
    - 대상 채널을 목표 위치까지 위로 swap하며 "4" 기록
<3> 이동("KBS1", 0), 이동("KBS2", 1)을 순서대로 실행한다.
<4> 누적된 버튼 문자열을 출력한다.

*/

fun main() {
    // [S0] 초기 입력/상태 준비
    // ----------------------------
    // 0) 입력
    // ----------------------------
    val n = readln().toInt() // [V4]
    val 채널들 = MutableList<String>(n) { readln() } // [V0]

    // 커서 위치와 출력 문자열
    var 커서 = 0 // [V1]
    val sb = StringBuilder() // [V2]

    // ----------------------------
    // 1) 대상 채널을 목표 위치로 옮기는 공통 동작
    // ----------------------------
    fun 이동(대상채널: String, 목표위치: Int) {
        var 대상채널위치 = 채널들.indexOf(대상채널) // [V3]

        // [S2] 커서를 대상 채널 위치까지 아래로 이동
        while (커서 < 대상채널위치) {
            sb.append("1")
            커서++
        }

        // [S3] 대상 채널을 목표 위치까지 위로 올림(swap 반복)
        while (대상채널위치 > 목표위치) {
            sb.append("4")
            채널들[대상채널위치] = 채널들[대상채널위치 - 1]
                .also { 채널들[대상채널위치 - 1] = 채널들[대상채널위치] }
            대상채널위치--
            커서--
        }
    }

    // ----------------------------
    // 2) KBS1 -> KBS2 순서로 정렬
    // ----------------------------
    // [S1] KBS1 이동 시작
    이동("KBS1", 0)
    // [S4] KBS2 이동 시작
    // [S5] 동일 규칙 반복
    이동("KBS2", 1)

    // ----------------------------
    // [S6] 버튼 시퀀스 출력
    // ----------------------------
    print(sb)
}
