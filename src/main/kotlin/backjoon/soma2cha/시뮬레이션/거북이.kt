package backjoon.`20260218`//  거북이

/*
[문제 요약]
- 명령 문자열(L, R, F, B)을 순서대로 수행하며 거북이를 이동시킨다.
- 거북이가 지나간 모든 좌표를 포함하는 최소 축 정렬 직사각형의 넓이를 구한다.
- 테스트케이스가 여러 개 주어지며 각 케이스별 넓이를 출력한다.

[핵심 아이디어]
- 모든 좌표를 저장하지 않고, 이동할 때마다 경계값 4개(minX, maxX, minY, maxY)만 갱신한다.
- 방향은 0~3 정수 + dx/dy 벡터로 관리하면 회전과 이동이 단순해진다.
  - L: (dir + 3) % 4
  - R: (dir + 1) % 4

[데이터 구조]
- dir, x, y: 현재 방향과 좌표
- minX/maxX/minY/maxY: 지나간 영역 경계
- dx/dy: 방향 벡터

[시뮬레이션 순서(각 테스트케이스)]
<1> 시작 좌표와 방향을 초기화한다. (x=0, y=0, dir=0)
<2> 명령 문자열을 왼쪽부터 1개씩 읽는다.
<3> L/R이면 방향만 회전, F/B이면 좌표를 이동한다.
<4> 이동이 일어날 때마다 min/max 경계를 즉시 갱신한다.
<5> 명령을 다 처리하면 width=maxX-minX, height=maxY-minY로 넓이를 계산한다.
<6> 모든 테스트케이스 결과를 모아 출력한다.

*/

fun main(args: Array<String>) {
    // [S0] 전체 입력/출력 버퍼 초기화
    // ----------------------------
    // 0) 입력
    // ----------------------------
    val n = readln().toInt()
    val sb = StringBuilder() // [V5]
    val dx = arrayOf(0, 1, 0, -1) // [V4] 하 우 상 좌 반시계방향...L은 레프트 반시계방향 그대로 니까 1 R은 시계방향이니까 3 더하면 됨.
    val dy = arrayOf(-1, 0, 1, 0) // [V4]

    // ----------------------------
    // 1) 테스트케이스 반복
    // ----------------------------
    repeat(n) { // [S9] 테스트케이스 남음 검사는 repeat가 담당
        // [S0] 테스트케이스별 상태 초기화
        val orderList = readln() // [V6]
        var dir = 0 // [V2]
        var x = 0 // [V0]
        var y = 0 // [V1]
        var minX = 0 // [V3]
        var minY = 0 // [V3]
        var maxX = 0 // [V3]
        var maxY = 0 // [V3]

        // 이동이 발생했을 때만 경계를 갱신하면 충분
        fun 경계갱신() {
            // [S6] 경계 갱신
            maxX = maxOf(maxX, x)
            minX = minOf(minX, x)
            maxY = maxOf(maxY, y)
            minY = minOf(minY, y)
        }

        // ----------------------------
        // 2) 명령 재생
        // ----------------------------
        for (order in orderList) {
            // [S1] 명령 1개 읽기
            // [S7] 명령 남음 검사는 for-loop가 담당
            // [S2] 명령 종류 분기
            when (order) {
                'L' -> dir = (dir + 3) % 4 // [S3] 반시계 방향이니 +3
                'R' -> dir = (dir + 1) % 4 // [S4] 같은 시계방향이니 +1
                'F' -> {
                    // [S5] 이동
                    x += dx[dir]
                    y += dy[dir]
                    경계갱신()
                }
                'B' -> {
                    // [S5] 이동
                    x -= dx[dir]
                    y -= dy[dir]
                    경계갱신()
                }
            }
        }

        // ----------------------------
        // 3) 넓이 계산
        // ----------------------------
        // [S8] 넓이 계산
        val width = maxX - minX
        val height = maxY - minY
        sb.append(width * height).append("\n")
    }

    // ----------------------------
    // [S10] 출력
    // 4) 출력
    // ----------------------------
    print(sb)
}