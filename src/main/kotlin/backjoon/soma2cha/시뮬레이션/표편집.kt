/*
[문제 요약]
- n개 행으로 구성된 표에서 커서를 움직이며 명령을 처리한다.
- 명령:
  U x: 위로 x칸 이동
  D x: 아래로 x칸 이동
  C: 현재 행 삭제
  Z: 가장 최근 삭제 복구
- 출력: 최종 행 상태 문자열(O/X)

[핵심 아이디어]
- 실제 배열 삭제/삽입을 매번 하면 느리므로, 양방향 연결 리스트를 "배열 링크"로 구현한다.
- 삭제 복구를 위해 스택에 (행, 위행, 아래행) 정보를 저장한다.

[데이터 구조]
- 위[i], 아래[i]: i행의 위/아래 이웃 인덱스
- 상태[i]: i행의 생존 여부(O/X)
- 삭제스택: 최근 삭제 이력
- 커서: 현재 선택 행

[시뮬레이션 순서(명령 재생)]
<1> 위/아래 링크 배열을 초기화해 표를 연결 리스트처럼 만든다.
<2> 명령을 순서대로 읽는다.
<3> U/D는 링크를 따라 커서를 이동한다.
<4> C는 현재행을 X로 바꾸고, 이웃 링크를 연결한 뒤 삭제 정보를 스택에 저장한다.
<5> Z는 스택에서 최근 삭제를 꺼내 링크와 상태를 복구한다.
<6> 모든 명령 후 상태 배열을 문자열로 합쳐 반환한다.

*/

class Solution8 {
    fun solution(n: Int, k: Int, cmd: Array<String>): String {
        // [S0] 초기상태 준비
        // ----------------------------
        // 0) 링크/상태 초기화
        // ----------------------------
        val 위 = MutableList(n) { it - 1 } // [V0]
        val 아래 = MutableList(n) { if (it == n - 1) -1 else it + 1 } // [V1]
        val 상태 = MutableList(n) { 'O' } // [V2]

        val 삭제스택 = ArrayDeque<IntArray>() // [V3] (행, 위, 아래)
        var 커서 = k // [V4]
        val 명령목록 = cmd // [V5]

        // ----------------------------
        // 1) 보조 함수
        // ----------------------------
        fun 위로이동(칸수: Int) {
            var 남은 = 칸수
            while (남은 > 0) {
                커서 = 위[커서]
                남은--
            }
        }

        fun 아래로이동(칸수: Int) {
            var 남은 = 칸수
            while (남은 > 0) {
                커서 = 아래[커서]
                남은--
            }
        }

        fun 삭제() {
            // [S3] 현재행 삭제 처리
            // 현재행 삭제 표시
            상태[커서] = 'X'

            // 복구용으로 링크 정보 저장
            val 위행 = 위[커서]
            val 아래행 = 아래[커서]
            삭제스택.addLast(intArrayOf(커서, 위행, 아래행))

            // 이웃 링크 재연결
            if (위행 != -1) 아래[위행] = 아래행
            if (아래행 != -1) 위[아래행] = 위행

            // 커서는 아래행 우선, 없으면 위행으로 이동
            커서 = if (아래행 != -1) 아래행 else 위행
        }

        fun 복구() {
            // [S4] 최근 삭제 복구 처리
            // 최근 삭제 이력 복원
            val 기록 = 삭제스택.removeLast()
            val 복구행 = 기록[0]
            val 위행 = 기록[1]
            val 아래행 = 기록[2]

            상태[복구행] = 'O'

            // 링크 다시 연결
            if (위행 != -1) 아래[위행] = 복구행
            if (아래행 != -1) 위[아래행] = 복구행
            위[복구행] = 위행
            아래[복구행] = 아래행
        }

        // ----------------------------
        // 2) 명령 처리
        // ----------------------------
        for (명령 in 명령목록) {
            // [S1] 명령 1개 읽기
            // [S5] 명령 남음 검사는 for-loop 반복이 담당
            when (명령[0]) {
                'U' -> 위로이동(명령.substring(2).toInt()) // [S2]
                'D' -> 아래로이동(명령.substring(2).toInt()) // [S2]
                'C' -> 삭제()
                'Z' -> 복구()
            }
        }

        // ----------------------------
        // [S6] 결과 반환
        // ----------------------------
        return 상태.joinToString("")
    }
}
