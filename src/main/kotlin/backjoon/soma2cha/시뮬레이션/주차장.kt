import java.util.PriorityQueue
import kotlin.math.abs

/*
[문제 요약]
- 주차칸 n개, 자동차 m대가 있고, 총 2m개의 이벤트(입차/출차)가 순서대로 주어진다.
- 입차 시:
  - 빈칸이 있으면 번호가 가장 작은 칸에 즉시 배정
  - 빈칸이 없으면 대기열에서 기다림
- 출차 시:
  - 해당 차의 주차 요금을 정산
  - 비워진 칸을 다시 가용칸으로 넣음
  - 대기차가 있으면 가장 앞 차를 즉시 배정
- 출력: 총 수입

[핵심 아이디어]
- "가장 작은 빈칸" 요구가 있으므로 PriorityQueue(최소 힙)로 가용 칸을 관리한다.
- 출차 때 정산하려면 "차 -> 주차칸" 매핑이 필요하다.
- 대기차는 FIFO이므로 ArrayDeque를 사용한다.

[데이터 구조]
- pq: 가용 주차칸(최소 번호 우선)
- dq: 입차 대기열
- 차랑별주차칸[차번호]: 현재 어떤 칸에 주차 중인지
- 주차공간[칸번호], 차목록[차번호]: 요금 계산용 단가

[시뮬레이션 순서(이벤트 스트림)]
<1> 가용 칸을 pq(최소 힙)에 전부 넣고, 대기열/매핑/수입을 초기화한다.
<2> 이벤트를 앞에서부터 하나씩 처리한다.
<3> 입차(+):
    - 빈칸 있으면 즉시 배정
    - 없으면 대기열에 추가
<4> 출차(-):
    - 매핑으로 주차칸 찾고 요금 정산
    - 칸을 가용 pq에 반납
    - 대기열이 있으면 즉시 한 대 배정
<5> 이벤트 종료 후 누적 수입을 출력한다.

*/
fun main(args: Array<String>) {
    // [S0] 초기상태 준비
    // ----------------------------
    // 0) 입력
    // ----------------------------
    val (n, m) = readln().split(" ").map { it.toInt() }
    val 주차공간 = IntArray(n + 1) { // [V3]
        if (it == 0) 0 else readln().toInt()
    }
    val 차목록 = IntArray(m + 1) { // [V4]
        if (it == 0) 0 else readln().toInt()
    }
    val actionList = IntArray(2 * m) { // [V6]
        readln().toInt()
    }

    // ----------------------------
    // 1) 상태 초기화
    // ----------------------------
    val 차랑별주차칸 = IntArray(m + 1) // [V2]
    var 수입 = 0 // [V5]

    // 가용 주차칸: 번호가 작은 칸부터 꺼내기 위해 최소 힙 사용
    val pq = PriorityQueue<Int>() // [V0]
    for (i in 1..n) pq.add(i)

    // 대기열(FIFO)
    val dq = ArrayDeque<Int>() // [V1]

    // ----------------------------
    // 2) 이벤트 순차 처리
    // ----------------------------
    actionList.forEach { x ->
        // [S1] 이벤트 1개 읽기
        val 차번호 = abs(x)
        // [S2] 입차 이벤트 검사
        if (x > 0) {
            // [S21] 빈칸 존재 검사
            if (pq.isNotEmpty()) {
                // [S3] 즉시 배정
                val 칸번호 = pq.poll()
                차랑별주차칸[차번호] = 칸번호
            } else {
                // [S4] 대기열 추가
                dq.addLast(차번호)
            }

        } else {
            // [S5] 출차 처리
            val 빠진칸번호 = 차랑별주차칸[차번호]
            차랑별주차칸[차번호] = 0
            수입 += 주차공간[빠진칸번호] * 차목록[차번호]
            pq.add(빠진칸번호)

            // [S6] 대기차 검사
            if (dq.isNotEmpty()) {
                // [S7] 대기차 즉시 배정
                val 기다린_차번호 = dq.removeFirst()
                val 칸번호 = pq.poll()
                차랑별주차칸[기다린_차번호] = 칸번호
            }
        }
        // [S8] 다음 이벤트 존재 여부는 forEach 반복이 담당
    }

    // ----------------------------
    // [S9] 결과 출력
    // ----------------------------
    println(수입)
}
