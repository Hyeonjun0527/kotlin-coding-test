package backjoon.soma2cha

import java.util.ArrayDeque
import java.util.TreeSet

/*
[문제 요약(현재 우리가 맞춘 버전)]
- 건물은 1층 ~ n층만 존재한다.
- 엘리베이터 초기 위치는 1층, 초기 방향은 위(UP).
- 1초에 1층 이동한다.
- 요청은 (부른시간, 부른층수) = [t, f] 형태로 주어지고, 입력은 시간 오름차순이다.
- 엘리베이터가 어떤 층에 "도착하는 순간", 그 층을 부른 요청은 즉시 처리된다(문 여닫는 시간 0).
- 처리할 요청이 없으면 엘리베이터는 현재 층에서 대기한다.
- 방향 규칙:
  <1> 현재 방향에 처리할 요청(현재층보다 위/아래)이 있으면 방향 유지
  <2> 현재 방향에 처리할 요청이 없고 반대쪽에만 있으면 방향 전환
  <3> 단, 1층/ n층 벽을 넘어갈 수 없으므로 벽이면 방향을 강제로 보정
- 출력: "요청이 처리된 층수"를 처리 순서대로 나열한 배열
요구사항목록 = [[0, 2],
            [2, 0],
            [3, 3]]
answer = [2, 6, 3]
*/

class Solution {

    /**
     * [이 풀이의 핵심 아이디어]
     * - 입력은 시간순이므로 "큐(Deque)로 앞에서 뽑기"가 가장 편하다.
     * - 대기 요청을 List로 들고 있으면 매초 전체를 훑어야 해서(느리고 코드도 지저분)
     *   -> "층별로 대기 개수"로 압축한다.
     *
     * [데이터 구조]
     * - 요구큐: 아직 시간이 안 돼서 대기로 못 넣은 "미래 요청들"
     * - 층별대기개수[f]: f층에 현재 쌓여 있는(아직 처리 안 된) 요청 개수
     * - 대기중인층(TreeSet): 대기 개수가 1 이상인 층만 모아둔 정렬 집합
     *   -> higher/lower로 현재층 기준 위/아래에 대기층이 있는지 빠르게 확인한다.
     *
     * [시뮬레이션 순서(매 1초마다)]
     * <1> 현재시간에 들어온 요청들을 요구큐에서 꺼내 "층별대기개수"에 누적
     * <2> 현재층에 대기 요청이 있으면 즉시 처리(개수만큼 결과에 현재층 기록)
     * <3> 대기가 아무 것도 없으면 엘리베이터는 멈춘 채로 시간만 1 증가
     * <4> 대기가 있으면 현재층 기준 위/아래에 대기층이 있는지 보고 방향 결정
     * <5> 벽(1층, n층)이면 넘어갈 수 없으므로 방향을 강제 보정
     * <6> 1층 이동 후 시간 1 증가
     *
     * [주석 템플릿 규칙]
     * - [S*] : 알고리즘 실행 흐름 단계
     * - [V*] : 핵심 상태 변수(데이터 저장소)
     * - 본문 진행 순서: [S0] 초기화 -> [S1..S10] 루프 처리 -> [S_END] 반환
     * - 상태 변수 선언/갱신 위치에는 [V*]를 함께 표기
     */
    fun solution(n: Int, 요구사항목록: Array<IntArray>): IntArray {
        // [S0] 초기화 구간 시작: 입력/자료구조/상태변수(V0~V6) 준비

        // ----------------------------
        // 0) 입력 크기
        // ----------------------------
        val k = 요구사항목록.size

        // ----------------------------
        // 1) 요청을 표현하는 자료형
        // - 시간: 요청이 발생한 시각
        // - 층: 요청이 발생한 층
        // ----------------------------
        data class 요청(val 시간: Int, val 층: Int)

        // ----------------------------
        // 2) 요구큐 만들기
        //
        // 왜 ArrayDeque?
        // - 입력이 시간순이므로, "앞에서 하나씩 꺼내기(removeFirst)"가 핵심
        // - drop(1)처럼 매번 새 리스트 만드는 방식은 O(n^2) 위험
        //
        // 요구큐의 의미:
        // - 아직 그 시간이 되지 않아 대기(층별대기)에 넣지 못한 "미래 요청들"
        // ----------------------------
        val 요구큐 = ArrayDeque<요청>() // [V1] 미래요청 저장소
        for (i in 0 until k) {
            val 부른시간 = 요구사항목록[i][0]
            val 부른층수 = 요구사항목록[i][1]
            요구큐.addLast(요청(부른시간, 부른층수))
        }

        // ----------------------------
        // 3) 층별대기개수
        //
        // 층별대기개수[f] = f층에 쌓인 "미처리 요청 개수"
        //
        // 좋은 점:
        // - 같은 층 요청이 10000개여도 "숫자 한 칸"으로 압축됨
        // - 현재층 처리 시 O(1)로 바로 처리 가능(리스트 전체 탐색 불필요)
        // ----------------------------
        val 층별대기개수 = IntArray(n + 1) // [V3] 층별 대기 요청 개수

        // ----------------------------
        // 4) 대기중인층(TreeSet)
        //
        // 의미:
        // - 층별대기개수가 1 이상인 층들만 모아둔 "정렬된 집합"
        //
        // 왜 필요?
        // - 방향을 정할 때 "현재층보다 위에 대기층이 있나? 아래에 있나?"를 빠르게 알아야 함
        // - higher(현재층): 현재층보다 큰 대기층 중 가장 가까운 것
        // - lower(현재층): 현재층보다 작은 대기층 중 가장 가까운 것
        // ----------------------------
        val 대기중인층 = TreeSet<Int>() // [V2] 현재 대기 존재 층 집합

        // ----------------------------
        // 5) 출력: 처리된 "층수"를 처리 순서대로 기록
        //
        // 예: 10층에서 2건 처리, 그 다음 3층에서 1건 처리라면
        // -> [10, 10, 3]
        // ----------------------------
        val 처리층수기록 = ArrayList<Int>(k) // [V6] 처리 순서 결과

        // ----------------------------
        // 6) 엘리베이터 상태
        // ----------------------------
        // [V0] 현재층, [V4] 현재시간, [V5] 방향
        var 현재시간 = 1   // [V4] (가정) 0초 호출 없음 → 1초부터 시작
        var 현재층 = 1      // [V0] 초기 위치: 1층
        var 위로 = true     // [V5] 초기 방향: 위(UP=true, DOWN=false)

        // ----------------------------
        // 7) 보조 함수: 대기 추가
        //
        // 요청이 (시간 t, 층 f)로 들어오면:
        // - 층별대기개수[f]++ 로 누적
        // - 대기중인층에 f를 넣어 "이 층에 대기가 있다" 표시
        // ----------------------------
        fun 대기추가(층: Int) {
            층별대기개수[층]++ // [S3][V3] 현재시간 유입 요청을 층별 대기로 누적
            대기중인층.add(층) // [S3][V2] 대기 중인 층 집합에 반영
        }

        // ----------------------------
        // 8) 보조 함수: 현재층 처리
        //
        // 현재층에 대기개수가 x개 있으면:
        // - 그 x개 요청은 "현재시간"에 모두 즉시 처리됨
        // - 출력에는 "층수"만 기록하므로 현재층을 x번 추가
        // - 처리 후 그 층 대기개수는 0으로 만들고, TreeSet에서도 제거
        // ----------------------------
        fun 현재층처리() {
            val 개수 = 층별대기개수[현재층]
            if (개수 == 0) return

            repeat(개수) { 처리층수기록.add(현재층) } // [S4][V6] 처리 순서 결과에 현재층 기록

            층별대기개수[현재층] = 0 // [S4][V3] 현재층 대기 개수 소진
            대기중인층.remove(현재층) // [S4][V2] 현재층 대기 플래그 제거
        }

        // ----------------------------
        // 9) 메인 시뮬레이션 루프(1초 단위)
        // ----------------------------
        while (true) {
            // [S1] 루프 시작

            // ----------------------------
            // [S2] 종료 조건 검사
            // - 미래 요청(요구큐)도 없고
            // - 현재 대기(대기중인층)도 없으면
            // => 더 이상 처리할 것이 없음. 종료.
            // ----------------------------
            if (요구큐.isEmpty() && 대기중인층.isEmpty()) break

            // ----------------------------
            // [S3] 현재시간 유입 요청을 대기 상태로 이동
            //
            // 입력이 시간순이므로:
            // - 요구큐.first().시간 == 현재시간 인 동안 계속 꺼내면 됨
            // ----------------------------
            while (요구큐.isNotEmpty() && 요구큐.first().시간 == 현재시간) {
                val 요청 = 요구큐.removeFirst()
                대기추가(요청.층) // [S3] 현재시간 요청을 대기 상태로 이동
            }

            // ----------------------------
            // [S4] 현재층에서 처리 가능한 요청 즉시 처리
            //
            // "도착하는 순간 처리"이므로 이동하기 전에 처리해야 1초 밀리지 않음
            // ----------------------------
            현재층처리() // [S4] 현재층 즉시 처리 단계

            // ----------------------------
            // [S5] 대기 존재 검사
            // [S6] 대기가 없으면 이동하지 않고 시간만 1 증가(대기)
            // ----------------------------
            if (대기중인층.isEmpty()) {
                현재시간++ // [S6][V4] 이동 없이 시간만 증가
                continue
            }

            // ----------------------------
            // [S7] 방향 결정
            //
            // - 위에 대기층이 있는지: higher(현재층) != null
            // - 아래에 대기층이 있는지: lower(현재층) != null
            //
            // 규칙:
            // - 현재 방향에 대기층이 없고 반대쪽에만 있으면 방향 전환
            // ----------------------------
            val 위에있나 = 대기중인층.higher(현재층) != null
            val 아래에있나 = 대기중인층.lower(현재층) != null

            if (위로) {
                if (!위에있나 && 아래에있나) 위로 = false // [S7][V5] 반대 방향으로 전환
            } else {
                if (!아래에있나 && 위에있나) 위로 = true // [S7][V5] 반대 방향으로 전환
            }

            // ----------------------------
            // [S8] 벽 보정
            //
            // - n층에서는 위로 갈 수 없으니 무조건 아래로
            // - 1층에서는 아래로 갈 수 없으니 무조건 위로
            // ----------------------------
            if (현재층 == n) 위로 = false // [S8][V5] 상단 벽 보정
            if (현재층 == 1) 위로 = true // [S8][V5] 하단 벽 보정

            // ----------------------------
            // [S9] 1층 이동
            // [S10] 시간 1 증가
            // ----------------------------
            현재층 += if (위로) 1 else -1 // [S9][V0] 현재 방향으로 1층 이동
            현재시간++ // [S10][V4] 이동 후 시간 증가
        }

        // ----------------------------
        // [S_END] 결과 반환
        // ----------------------------
        return 처리층수기록.toIntArray()
    }
}
