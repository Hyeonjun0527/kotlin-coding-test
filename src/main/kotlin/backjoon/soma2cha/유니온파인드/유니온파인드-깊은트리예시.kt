// BOJ: https://www.acmicpc.net/problem/1717
/*
[문제 요약]
- 깊은 체인 형태의 유니온파인드 트리를 만든 뒤,
  find(1) 한 번으로 경로 압축이 어떻게 일어나는지 확인한다.
*/

class Solution {

    /**
     * [핵심 아이디어]
     * - 1 -> 2 -> 3 -> ... -> n 형태의 깊은 트리를 만든 후 find(1)을 수행한다.
     * - find 재귀에서 parent를 루트로 재할당하면 경로 압축이 완료된다.
     *
     * [데이터 구조]
     * - V0 노드수(Int): 노드 수
     * - V1 부모(IntArray): 부모 배열
     * - V2 시작노드(Int): find 시작 노드
     *
     * [탐색/처리 순서]
     * <1> parent 배열 초기화
     * <2> 깊은 체인 트리 구성
     * <3> 시작노드에서 find 호출
     * <4> 압축된 parent 배열 반환
     *
     * [주석 템플릿 규칙]
     * - [S*] : 실행 단계
     * - [V*] : 상태 변수
     * - 핵심 분기/갱신 라인에는 [S4]를 표기
     */
    fun solution(노드개수: Int): IntArray {
        // [S0] 초기화
        val 노드수 = 노드개수 // [V0]
        if (노드수 <= 0) return intArrayOf() // [S0]

        val 부모 = IntArray(노드수 + 1) { it } // [V1]

        // [S1] 깊은 체인 트리 구성: 1->2->...->n
        for (번호 in 1 until 노드수) {
            부모[번호] = 번호 + 1 // [S1][V1]
        }
        부모[노드수] = 노드수 // [S1][V1]

        // [S2] find 함수 정의
        fun find(노드: Int): Int {
            if (부모[노드] == 노드) return 노드
            부모[노드] = find(부모[노드]) // [S4][V1] 핵심 갱신: 경로 압축
            return 부모[노드]
        }

        // [S3] 시작 노드 압축 수행
        val 시작노드 = 1 // [V2]
        find(시작노드) // [S3][V2]

        // [S_END] 압축 완료 배열 반환
        return 부모
    }
}
