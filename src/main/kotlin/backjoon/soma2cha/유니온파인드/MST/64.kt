// BOJ: https://www.acmicpc.net/problem/1197
/*
[문제 요약]
- 정점 n개와 간선 목록이 주어질 때,
  모든 정점을 연결하는 최소 스패닝 트리(MST) 비용을 구한다.
- 연결이 불가능하면 -1을 반환한다.
*/

class Solution {

    /**
     * [핵심 아이디어]
     * - 크루스칼 알고리즘으로 간선을 가중치 오름차순으로 선택한다.
     * - 간선 채택 전 유니온파인드로 사이클 여부를 확인한다.
     *
     * [데이터 구조]
     * - V0 정점수(Int): 정점 개수
     * - V1 정렬간선목록(List<IntArray>): 가중치 기준 정렬 간선
     * - V2 부모(IntArray): 유니온파인드 부모 배열
     * - V3 사용간선수(Int): 채택한 간선 수
     * - V4 누적가중치(Int): 누적 비용
     * - V5 시작인덱스(Int): 정점 번호 시작값(0 또는 1)
     *
     * [처리 순서]
     * <1> 입력과 상태 변수 초기화
     * <2> 간선 정렬
     * <3> find/union 정의
     * <4> 정렬 간선 순회하며 사이클이 아니면 채택
     * <5> 사용 간선 수가 n-1인지 확인
     *
     * [주석 템플릿 규칙]
     * - [S*] : 실행 단계
     * - [V*] : 상태 변수
     * - 핵심 분기/확장 라인에 [S4]를 명시
     */
    fun solution(정점개수: Int, 간선목록: Array<IntArray>): Int {
        // [S0] 상태 초기화
        val 정점수 = 정점개수 // [V0]
        val 시작인덱스 = if (간선목록.any { it[0] == 0 || it[1] == 0 }) 0 else 1 // [V5]
        val 부모 = IntArray(정점수 + 시작인덱스) { it } // [V2]
        var 사용간선수 = 0 // [V3]
        var 누적가중치 = 0 // [V4]

        // [S1] 간선 정렬
        val 정렬간선목록 = 간선목록.sortedBy { it[2] } // [V1]

        // [S2] 유니온파인드 함수 정의
        fun find(정점: Int): Int {
            if (부모[정점] == 정점) return 정점
            부모[정점] = find(부모[정점]) // [S4][V2] 경로 압축
            return 부모[정점]
        }

        fun union(정점A: Int, 정점B: Int) {
            val 대표A = find(정점A)
            val 대표B = find(정점B)
            if (대표A != 대표B) 부모[대표B] = 대표A // [S4][V2] 서로 다른 집합만 병합
        }

        // [S3] 간선 순회
        for (간선 in 정렬간선목록) {
            if (사용간선수 == 정점수 - 1) break
            val 시작정점 = 간선[0]
            val 끝정점 = 간선[1]
            val 가중치 = 간선[2]

            // [S4] 핵심 분기: 사이클 검사 후 채택
            // 시작정점과 끝정점의 대표가 같으면 이미 같은 연결요소다.
            // 이 간선을 추가하면 사이클이 생겨 MST 조건을 깨므로 건너뛴다.
            if (find(시작정점) == find(끝정점)) continue // [S4]
            union(시작정점, 끝정점)
            사용간선수++ // [S4][V3]
            누적가중치 += 가중치 // [S4][V4]
        }

        // [S5] 연결성 확인
        if (사용간선수 != 정점수 - 1) return -1 // [S5][V3]

        // [S_END] 정답 반환
        return 누적가중치
    }
}
