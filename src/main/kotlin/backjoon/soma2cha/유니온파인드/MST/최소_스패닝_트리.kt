package backjoon.soma2cha.유니온파인드.MST/*
[문제 요약]
그래프가 주어졌을 때, 그 그래프의 최소 스패닝 트리를 구하는 프로그램을 작성하시오.

최소 스패닝 트리는, 주어진 그래프의 모든 정점들을 연결하는 부분 그래프 중에서 그 가중치의 합이 최소인 트리를 말한다.
첫째 줄에 최소 스패닝 트리의 가중치를 출력한다.

- 간선 목록으로 최소 스패닝 트리의 총 가중치를 구한다.
- 연결 불가능하면 -1을 반환한다.
*/

class Solution {

    /**
     * [핵심 아이디어]
     * - 크루스칼 + 유니온파인드로 가장 가벼운 간선부터 선택한다.
     * - 이미 같은 집합이면 사이클이므로 건너뛴다.
     *
     * [데이터 구조]
     * - V0 정점수(Int): 정점 개수
     * - V1 정렬간선목록(List<IntArray>): 가중치 정렬 간선 목록
     * - V2 부모(IntArray): 대표 노드 배열
     * - V3 사용간선수(Int): 채택 간선 수
     * - V4 누적가중치(Int): 누적 가중치
     *
     * [처리 순서]
     * <1> 상태 초기화
     * <2> 간선 정렬
     * <3> find/union 정의
     * <4> 간선 순회하며 MST 구성
     * <5> 연결성 검증 후 반환
     *
     * [주석 템플릿 규칙]
     * - [S*] : 실행 단계
     * - [V*] : 상태 변수
     * - 핵심 분기/확장 라인에 [S4]를 명시
     */
    /*
    * 간선목록 : [[1, 2, 2],[1, 3, 3],[2, 3, 4],[2, 4, 5],[3, 4, 6]]
    * */
    fun solution(정점개수: Int, 간선목록: Array<IntArray>): Int {
        // [S0] 초기화
        val 정점수 = 정점개수 // [V0]
        val 부모 = IntArray(정점수 + 1) { it } // [V2]
        var 사용간선수 = 0 // [V3]
        var 누적가중치 = 0 // [V4]

        // [S1] 가중치 오름차순 정렬
        val 정렬간선목록 = 간선목록.sortedBy { it[2] } // [V1]

        // [S2] find/union
        fun find(정점: Int): Int {
            if (부모[정점] == 정점) return 정점
            부모[정점] = find(부모[정점]) // [S4][V2]
            return 부모[정점]
        }

        fun union(정점A: Int, 정점B: Int) {
            val 대표A = find(정점A)
            val 대표B = find(정점B)
            if (대표A != 대표B) 부모[대표B] = 대표A // [S4][V2]
        }

        // [S3] 간선 순회
        /**
         * 트리면 간선수 == 정점수 - 1가 되어야함.
         * 그런데 간선수 > 정점수임. 간선목록만 전부돌면 됨
         *
         */
        for (간선 in 정렬간선목록) {

            //완성검사
            if (사용간선수 == 정점개수 - 1) break

            //사이클검사
            val a = 간선[0];
            val b = 간선[1];
            val 가중치 = 간선[2]
            if (find(a) == find(b)) continue
            //연결처리
            union(a, b)
            사용간선수++
            누적가중치 += 가중치
        }
        if (사용간선수 != 정점개수 - 1) return -1
        return 누적가중치
    }
}
