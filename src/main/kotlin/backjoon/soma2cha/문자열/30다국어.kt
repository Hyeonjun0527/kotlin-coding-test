package backjoon.soma2cha.문자열// BOJ: https://www.acmicpc.net/problem/10610

/*
[문제 요약]
- 숫자 문자열의 자릿수를 재배치해 만들 수 있는 30의 배수 중 가장 큰 수를 구한다.
- 만들 수 없으면 -1을 반환한다.

예시
| N | return |
|---|---|
| "102" | "210" |
| "2931" | "-1" |

의미
- N: 입력 숫자 문자열
- return: 만들 수 있는 가장 큰 30의 배수 문자열, 없으면 "-1"
*/

class Solution {

    /**
     * [핵심 아이디어]
     * - 30의 배수 조건은 두 가지다.
     *   1) 10의 배수: 숫자 0이 최소 1개 필요
     *   2) 3의 배수: 모든 자릿수 합이 3의 배수
     * - 조건을 만족하면 자릿수를 내림차순 정렬한 값이 가장 큰 수다.
     *
     * [데이터 구조]
     * - V0 문자열길이(Int): 입력 문자열 길이
     * - V1 자릿수합(Int): 모든 자릿수 합
     * - V2 영포함여부(Boolean): 0 포함 여부
     * - V3 현재문자(Char): 순회 중인 문자
     * - V4 현재숫자(Int): 현재문자를 숫자로 변환한 값
     * - V5 정렬자릿수목록(List<Char>): 내림차순 정렬된 자릿수 목록
     * - V6 결과문자열(String): 최종 정답 문자열
     *
     * [처리 순서]
     * <1> 자릿수합과 0 포함 여부 계산
     * <2> 30 배수 가능 여부 검사
     * <3> 가능하면 자릿수 내림차순 정렬
     * <4> 정렬 결과를 문자열로 합쳐 반환
     *
     * [주석 템플릿 규칙]
     * - [S*] : 실행 단계
     * - [V*] : 상태 변수
     * - 핵심 분기/확장 라인에는 [S4]를 명시
     */
    fun solution(숫자문자열: String): String {
        // [S0] 상태 초기화
        val 문자열길이 = 숫자문자열.length // [V0]
        if (문자열길이 == 0) return "-1" // [S0][V0]

        var 자릿수합 = 0 // [V1]
        var 영포함여부 = false // [V2]

        // [S1] 자릿수 정보 집계
        for (현재문자 in 숫자문자열) { // [V3]
            val 현재숫자 = 현재문자 - '0' // [V4]
            자릿수합 += 현재숫자 // [V1][V4]
            if (현재숫자 == 0) 영포함여부 = true // [V2][V4]
        }

        // [S2] 30 배수 가능 여부 검사
        if (!영포함여부 || 자릿수합 % 3 != 0) return "-1" // [S4][V1][V2] 핵심 분기

        // [S3] 내림차순 정렬
        val 정렬자릿수목록 = 숫자문자열.toList().sortedDescending() // [V5]

        // [S_END] 정답 반환
        val 결과문자열 = 정렬자릿수목록.joinToString("") // [V6]
        return 결과문자열
    }
}
