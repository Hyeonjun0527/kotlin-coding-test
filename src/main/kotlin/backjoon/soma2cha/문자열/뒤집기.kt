package backjoon.soma2cha.문자열// BOJ: https://www.acmicpc.net/problem/1439

/*
[문제 요약]
- 0/1 문자열에서 연속 구간을 뒤집어(0↔1) 모든 문자를 같게 만들 때 최소 뒤집기 횟수를 구한다.
- 핵심은 연속된 0 묶음 개수와 1 묶음 개수를 세고, 둘 중 작은 값을 선택하는 것이다.

예시
| S | return |
|---|---|
| "0001100" | 1 |

의미
- S: 0과 1로만 이루어진 문자열
- return: 모든 문자를 같게 만들기 위한 최소 뒤집기 횟수
*/

class Solution {

    /**
     * [핵심 아이디어]
     * - 문자열에서 "문자가 바뀌는 지점"이 새로운 그룹의 시작이다.
     * - 0 그룹 수와 1 그룹 수를 각각 세면,
     *   결국 한쪽 그룹만 뒤집으면 모두 같은 값이 되므로 정답은 min(0그룹, 1그룹)이다.
     *
     * [데이터 구조]
     * - V0 문자열길이(Int): 입력 문자열 길이
     * - V1 이전문자(Char): 직전 문자
     * - V2 영그룹수(Int): 연속된 0 그룹 개수
     * - V3 일그룹수(Int): 연속된 1 그룹 개수
     * - V4 현재문자(Char): 현재 순회 문자
     *
     * [처리 순서]
     * <1> 빈 문자열 예외 처리
     * <2> 첫 문자로 첫 그룹 카운트 반영
     * <3> 두 번째 문자부터 순회하며 그룹 경계 탐지
     * <4> 문자 변경 시 새 그룹 카운트 증가
     * <5> min(영그룹수, 일그룹수) 반환
     *
     * [주석 템플릿 규칙]
     * - [S*] : 실행 단계
     * - [V*] : 상태 변수
     * - 핵심 분기/확장 라인에는 [S4]를 명시
     */
    fun solution(문자열S: String): Int {
        // [S0] 상태 초기화
        val 문자열길이 = 문자열S.length // [V0]
        if (문자열길이 == 0) return 0 // [S0][V0]

        var 이전문자 = 문자열S[0] // [V1]
        var 영그룹수 = 0 // [V2]
        var 일그룹수 = 0 // [V3]

        // [S1] 첫 그룹 반영
        if (이전문자 == '0') 영그룹수++ else 일그룹수++

        // [S2] 문자열 순회
        for (인덱스 in 1 until 문자열길이) {
            val 현재문자 = 문자열S[인덱스] // [V4]

            // [S3] 그룹 경계 검사 현재문자와 이전문자가 같으면 스킵. 같은 그룹임. 다르면 다른 그룹이 되엇으니 그룹 수를 세야함.
            if (현재문자 == 이전문자) continue

            // [S4] 핵심 갱신: 새 그룹 카운트 증가 및 이전문자 갱신
            if (현재문자 == '0') 영그룹수++ else 일그룹수++ // [S4][V2][V3][V4]
            이전문자 = 현재문자 // [S4][V1][V4]
        }

        // [S_END] 정답 반환
        return minOf(영그룹수, 일그룹수)
    }
}
