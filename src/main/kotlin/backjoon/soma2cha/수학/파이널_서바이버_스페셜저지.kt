// BOJ: https://www.acmicpc.net/problem/34003

/*
[문제 요약]
- 현재 8x8 보드에서 O 블록만 남아 있고, 이후 남은 블록이 4개가 될 때까지
  매 단계에서 남은 블록 중 하나가 균등 확률로 사라진다.
- 플레이어는 7x7 꼭짓점 중 하나에 서며, 인접한 4칸 중 하나라도 최종 4칸에 포함되면 생존한다.
- 우승 확률이 최대가 되는 꼭짓점 (행, 열)과 그 확률을 구한다.
*/
class Solution {

    data class FinalSurvivorResult(
        val 행: Int,
        val 열: Int,
        val 확률: Double
    )

    /**
     * [핵심 아이디어]

    블록이 하나씩 랜덤하게 사라져서 **최종 4개만 남는 과정**은,
    “현재 남아있는 N개의 블록 중에서, 최종 생존 블록 4개를 **균등하게** 고르는 것”
    과 **완전히 동일**하다. (대칭성/균등성)
    그래서 어떤 꼭짓점의 주변 2×2에 남아있는 블록 수를 `m`이라 하면:
    * 전체 남은 블록 수: `N`
    * 최종 4개가 전부 “주변 2×2 밖”에서만 뽑히는 경우가 “죽는 경우”
    생존 확률 = 1 - (죽는확률)
    죽는 확률 = 내 4칸 주변 밖에서 뽑히는 경우 / 남아있는 n개 블록중에서 최종 4개 블록 고르는 모든 경우의 수
     * - "사라지는 순서가 랜덤"인 과정은 "최종 4개 블록 조합이 균등"한 것과 동치다.
     * - 전체 생존 블록 수를 M, 선택한 꼭짓점 인접 생존 블록 수를 s라 하면:
     *   생존 확률 = 1 - C(M - s, 4) / C(M, 4)
     * - 7x7 꼭짓점을 전부 검사해 최대 확률 위치를 고르면 된다.
     *
     * [데이터 구조]
     * - V0 입력보드(Array<String>): 8x8 입력 보드
     * - V1 생존표(Array<BooleanArray>): 각 칸의 현재 생존 여부
     * - V2 전체생존수(Int): 보드 전체 O 개수
     * - V3 최고행(Int): 현재 최고 확률 꼭짓점 행(1-based)
     * - V4 최고열(Int): 현재 최고 확률 꼭짓점 열(1-based)
     * - V5 최고확률(Double): 현재 최대 우승 확률
     * - V6 주변생존수(Int): 현재 꼭짓점 인접 4칸 중 O 개수
     * - V7 현재확률(Double): 현재 꼭짓점 우승 확률
     * - V8 전체조합수(Long): C(M, 4)
     * - V9 제외조합수(Long): C(M - s, 4)
     * - V10 결과(FinalSurvivorResult): 최종 반환 객체
     *
     * [시뮬레이션(또는 탐색) 순서]
     * <1> 보드를 파싱해 생존표와 전체생존수를 만든다.
     * <2> C(M, 4)를 미리 계산한다.
     * <3> 7x7 모든 꼭짓점을 순회한다.
     * <4> 각 꼭짓점에서 인접 4칸의 생존 블록 수 s를 센다.
     * <5> 조합식으로 생존 확률을 계산한다.
     * <6> 최대 확률이면 위치/확률을 갱신한다.
     * <7> 최종 결과 객체를 반환한다.
     *
     * [주석 템플릿 규칙]
     * - [S*] : 실행 단계
     * - [V*] : 핵심 상태 변수
     * - 핵심 분기/확장 라인에는 [S4]를 명시
     */
    fun solution(블록상태: Array<String>): FinalSurvivorResult {
        // [S0] 상태 초기화
        val 입력보드 = 블록상태 // [V0]
        val 생존표 = Array(8) { BooleanArray(8) } // [V1]
        var 전체생존수 = 0 // [V2]
        var 최고행 = 1 // [V3]
        var 최고열 = 1 // [V4]
        var 최고확률 = -1.0 // [V5]

        // [S1] 보드 파싱 + 전체 생존 블록 수 집계
        for (x in 0 until 8) {
            for (y in 0 until 8) {
                if (입력보드[x][y] == 'O') {
                    생존표[x][y] = true // [V1]
                    전체생존수++ // [V2]
                }
            }
        }

        // [S2] 전체 조합 수 C(M, 4) 계산
        val 전체조합수 = combination4(전체생존수) // [V8]

        // [S3] 7x7 모든 꼭짓점 완전탐색
        for (꼭짓점행 in 1..7) {
            for (꼭짓점열 in 1..7) {
                var 주변생존수 = 0 // [V6]
                val x = 꼭짓점행 - 1
                val y = 꼭짓점열 - 1

                if (생존표[x][y]) 주변생존수++ // [V6]
                if (생존표[x][y + 1]) 주변생존수++ // [V6]
                if (생존표[x + 1][y]) 주변생존수++ // [V6]
                if (생존표[x + 1][y + 1]) 주변생존수++ // [V6]

                // [S4] 핵심 분기: 바깥생존수 기준으로 현재 꼭짓점 생존 확률 계산
                val 바깥생존수 = 전체생존수 - 주변생존수
                val 현재확률 = if (바깥생존수 < 4) {
                    1.0 // [S4][V2][V6][V7]
                } else {
                    val 제외조합수 = combination4(바깥생존수) // [V9] = C(M-s, 4)
                    1.0 - 제외조합수.toDouble() / 전체조합수.toDouble() // [S4][V7][V8][V9]
                }

                // [S5] 최대 확률 갱신
                if (현재확률 > 최고확률) { // [S4][V5][V7]
                    최고확률 = 현재확률 // [V5]
                    최고행 = 꼭짓점행 // [V3]
                    최고열 = 꼭짓점열 // [V4]
                }
            }
        }

        // [S6] 결과 객체 생성
        val 결과 = FinalSurvivorResult(최고행, 최고열, 최고확률) // [V10]

        // [S_END] 결과 반환
        return 결과
    }

    private fun combination4(n: Int): Long {
        if (n < 4) return 0L
        return n.toLong() * (n - 1L) * (n - 2L) * (n - 3L) / 24L
    }
}
