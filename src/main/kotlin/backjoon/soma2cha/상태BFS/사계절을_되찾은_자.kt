// BOJ: https://www.acmicpc.net/problem/34006
import java.util.ArrayDeque

/*
[문제 요약]
- 세 게이지 (봄, 여름, 가을)는 항상 합이 3N이고,
  각 값은 항상 0 초과 2N 미만(안전 범위)을 유지해야 한다.
- 공격 A/B/C를 맞으면 아래처럼 게이지가 이동한다.
  A: 봄 +a, 가을 -a
  B: 봄 -b, 여름 +b
  C: 여름 -c, 가을 +c
- 시작 상태 (A, B, C)에서 목표 상태 (N, N, N)까지 가는
  최소 피격 횟수와, 그중 사전순으로 가장 앞선 공격 문자열을 구한다.
*/
class Solution {

    /**
     * [핵심 아이디어]
     * - 상태를 (봄, 여름) 2차원으로 압축하면 가을은 총합(3N)으로 자동 복원된다.
     * - 각 공격은 비용 1 간선이므로 최소 횟수는 BFS가 보장한다.
     * - BFS 확장을 A -> B -> C 순서로 고정하면,
     *   같은 최단거리 후보 중 사전순 최소 문자열이 자동으로 선택된다.
     *
     * [데이터 구조]
     * - V0 기준N(Int): 목표 게이지 값 N
     * - V1 초기봄(Int): 시작 봄 게이지 A
     * - V2 초기여름(Int): 시작 여름 게이지 B
     * - V3 초기가을(Int): 시작 가을 게이지 C
     * - V4 A변화량(Int): 공격 A 변화량 a
     * - V5 B변화량(Int): 공격 B 변화량 b
     * - V6 C변화량(Int): 공격 C 변화량 c
     * - V7 총합(Int): 시작 게이지 총합(=3N)
     * - V8 안전상한(Int): 2N
     * - V9 한줄길이(Int): 상태 인덱싱 폭(0..2N)
     * - V10 상태개수(Int): 전체 상태 개수
     * - V11 시작상태(Int): 시작 상태 번호
     * - V12 목표상태(Int): 목표 상태 번호
     * - V13 최단횟수(IntArray): 상태별 최소 피격 횟수
     * - V14 직전상태(IntArray): 경로 복원용 이전 상태
     * - V15 직전공격(ByteArray): 경로 복원용 공격 종류(0=A,1=B,2=C)
     * - V16 큐(ArrayDeque<Int>): BFS 큐
     * - V17 최소피격횟수(Int): 목표 상태 최단 거리
     * - V18 공격순서(CharArray): 복원된 정답 문자열
     *
     * [시뮬레이션(또는 탐색) 순서]
     * <1> 상태 번호 함수와 안전 상태 판별 함수를 정의한다.
     * <2> 시작/목표 상태와 BFS 배열을 초기화한다.
     * <3> 시작 상태를 큐에 넣고 BFS를 수행한다.
     * <4> 매 상태에서 A, B, C를 순서대로 확장한다.
     * <5> 목표 도달 실패면 (-1, "") 반환한다.
     * <6> 직전 배열로 경로를 역추적해 공격 문자열을 만든다.
     *
     * [주석 템플릿 규칙]
     * - [S*] : 실행 단계
     * - [V*] : 핵심 상태 변수
     * - 핵심 분기/확장 라인에는 [S4]를 명시
     */
    fun solution(N: Int, A: Int, B: Int, C: Int, a: Int, b: Int, c: Int): Pair<Int, String> {
        // [S0] 상태 초기화
        val 기준N = N // [V0]
        val 초기봄 = A // [V1]
        val 초기여름 = B // [V2]
        val 초기가을 = C // [V3]
        val A변화량 = a // [V4]
        val B변화량 = b // [V5]
        val C변화량 = c // [V6]
        val 총합 = 3 * 기준N // [V7]
        val 안전상한 = 2 * 기준N // [V8]
        val 한줄길이 = 안전상한 + 1 // [V9]
        val 상태개수 = 한줄길이 * 한줄길이 // [V10]

        // [S1] 상태 변환/유효성 검사 함수
        fun 상태번호(봄: Int, 여름: Int): Int = 봄 * 한줄길이 + 여름

        fun 안전상태인가(봄: Int, 여름: Int): Boolean {
            if (봄 <= 0 || 봄 >= 안전상한) return false
            if (여름 <= 0 || 여름 >= 안전상한) return false
            val 가을 = 총합 - 봄 - 여름
            if (가을 <= 0 || 가을 >= 안전상한) return false
            return true
        }

        // [S2] 시작/목표 상태 + BFS 배열 초기화
        val 시작상태 = 상태번호(초기봄, 초기여름) // [V11]
        val 목표상태 = 상태번호(기준N, 기준N) // [V12]
        val 최단횟수 = IntArray(상태개수) { -1 } // [V13]
        val 직전상태 = IntArray(상태개수) { -1 } // [V14]
        val 직전공격 = ByteArray(상태개수) // [V15]
        val 큐 = ArrayDeque<Int>() // [V16]

        // [S3] 시작 상태 삽입
        최단횟수[시작상태] = 0 // [V13]
        큐.addLast(시작상태) // [V16]

        // [S4] BFS (A -> B -> C 순서 확장)
        while (큐.isNotEmpty()) {
            val 현재상태 = 큐.removeFirst()
            if (현재상태 == 목표상태) break

            val 현재봄 = 현재상태 / 한줄길이
            val 현재여름 = 현재상태 % 한줄길이

            // [S4] A: 봄 += A변화량
            run {
                val 다음봄 = 현재봄 + A변화량
                val 다음여름 = 현재여름
                if (안전상태인가(다음봄, 다음여름)) {
                    val 다음상태 = 상태번호(다음봄, 다음여름)
                    if (최단횟수[다음상태] == -1) { // [S4][V13]
                        최단횟수[다음상태] = 최단횟수[현재상태] + 1 // [S4][V13]
                        직전상태[다음상태] = 현재상태 // [S4][V14]
                        직전공격[다음상태] = 0 // [S4][V15]
                        큐.addLast(다음상태) // [S4][V16]
                    }
                }
            }

            // [S4] B: 여름 += B변화량, 봄 -= B변화량
            run {
                val 다음봄 = 현재봄 - B변화량
                val 다음여름 = 현재여름 + B변화량
                if (안전상태인가(다음봄, 다음여름)) {
                    val 다음상태 = 상태번호(다음봄, 다음여름)
                    if (최단횟수[다음상태] == -1) { // [S4][V13]
                        최단횟수[다음상태] = 최단횟수[현재상태] + 1 // [S4][V13]
                        직전상태[다음상태] = 현재상태 // [S4][V14]
                        직전공격[다음상태] = 1 // [S4][V15]
                        큐.addLast(다음상태) // [S4][V16]
                    }
                }
            }

            // [S4] C: 여름 -= C변화량
            run {
                val 다음봄 = 현재봄
                val 다음여름 = 현재여름 - C변화량
                if (안전상태인가(다음봄, 다음여름)) {
                    val 다음상태 = 상태번호(다음봄, 다음여름)
                    if (최단횟수[다음상태] == -1) { // [S4][V13]
                        최단횟수[다음상태] = 최단횟수[현재상태] + 1 // [S4][V13]
                        직전상태[다음상태] = 현재상태 // [S4][V14]
                        직전공격[다음상태] = 2 // [S4][V15]
                        큐.addLast(다음상태) // [S4][V16]
                    }
                }
            }
        }

        // [S5] 도달 실패 처리
        if (최단횟수[목표상태] == -1) {
            // [S_END] 불가능 반환
            return Pair(-1, "")
        }

        // [S6] 경로 역추적
        val 최소피격횟수 = 최단횟수[목표상태] // [V17]
        val 공격순서 = CharArray(최소피격횟수) // [V18]
        var 현재 = 목표상태
        var 채우기위치 = 최소피격횟수 - 1

        while (현재 != 시작상태) {
            val 공격종류 = 직전공격[현재].toInt()
            공격순서[채우기위치--] = when (공격종류) {
                0 -> 'A'
                1 -> 'B'
                else -> 'C'
            }
            현재 = 직전상태[현재]
        }

        // [S_END] 정답 반환
        return Pair(최소피격횟수, 공격순서.concatToString())
    }
}
