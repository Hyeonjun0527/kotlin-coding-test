package backjoon.`20251125`

import java.util.ArrayDeque

/*
[문제 요약(현재 우리가 맞춘 버전)]
- 정점은 1번 ~ n번까지 존재한다.
- 간선은 무방향이며, 간선목록의 각 원소는 [a, b] 형태이다.
- 시작정점에서 BFS를 수행한다.
- 인접 정점은 "번호 오름차순"으로 방문한다.
- 출력: BFS 방문 순서대로 정점 번호를 담은 배열
간선목록 = [[1,2], [1,3], [2,4], [3,4]]
시작정점 = 1
answer = [1, 2, 3, 4]
*/

class Solution {

    /**
     * [이 풀이의 핵심 아이디어]
     * - graph를 인접 리스트로 만든 뒤, 각 리스트를 오름차순 정렬한다.
     * - BFS 큐를 사용해 "먼저 발견한 정점부터" 순서대로 처리한다.
     * - 정점을 큐에 넣는 순간 방문 처리해서 중복 삽입을 막는다.
     *
     * [데이터 구조]
     * - graph(Array<MutableList<Int>>): 정점별 인접 정점 목록
     * - visited(BooleanArray): 이미 발견된 정점인지 여부
     * - 방문순서(ArrayList<Int>): BFS 처리 순서 기록
     * - 큐(ArrayDeque<Int>): BFS 탐색 대기열
     *
     * [시뮬레이션 순서]
     * <1> graph 구성 후 인접 리스트 오름차순 정렬
     * <2> 시작정점을 큐에 넣고 방문 처리
     * <3> 큐에서 정점을 꺼내 방문순서에 기록
     * <4> 현재 정점의 이웃을 순회하며 미방문 정점을 방문 처리 후 큐에 삽입
     * <5> 큐가 빌 때까지 <3>~<4> 반복
     *
     * [주석 템플릿 규칙]
     * - [S*] : 알고리즘 실행 흐름 단계
     * - [V*] : 핵심 상태 변수(데이터 저장소)
     * - 본문 진행 순서: [S0] 초기화 -> [S1..S5] BFS 처리 -> [S_END] 반환
     * - 상태 변수 선언/갱신 위치에는 [V*]를 함께 표기
     */
    fun solution(n: Int, 간선목록: Array<IntArray>, 시작정점: Int): IntArray {
        // [S0] 초기화 구간 시작: graph/visited(V0~V1) 준비

        // ----------------------------
        // 0) graph 생성 (1-indexed), 방문목록 생성
        // ----------------------------
        val 인접목록 = Array(n + 1) { mutableListOf<Int>() } // [V0] 인접 리스트 graph
        val 방문순서 = mutableListOf<Int>()// [V2] BFS 방문 순서 결과
        val 방문목록 = BooleanArray(n + 1) // [V1] 정점 방문 여부

        for (간선 in 간선목록) {
            val a = 간선[0]
            val b = 간선[1]
            인접목록[a].add(b)
            인접목록[b].add(a)
        }

        for (정점 in 1..n) {
            인접목록[정점].sort()
        }

        fun bfs(시작정점: Int){
            val 큐 = ArrayDeque<Int>() // [V3] BFS 대기 큐

            // ----------------------------
            // [S1] 시작 정점 방문 처리 + 큐 삽입
            // ----------------------------
            방문목록[시작정점] = true // [S1][V1] 시작 정점 발견 처리
            큐.addLast(시작정점) // [S1][V3] 시작 정점부터 탐색 시작

            // ----------------------------
            // [S2] BFS 메인 루프 시작
            // - 큐가 빌 때까지 반복
            // ----------------------------
            while (큐.isNotEmpty()) {
                // ----------------------------
                // [S3] 큐에서 현재 정점 꺼내기 + 방문 기록
                // ----------------------------
                val 현재정점 = 큐.removeFirst()
                방문순서.add(현재정점) // [S3][V2] 방문 순서에 기록

                // ----------------------------
                // [S4] 현재 정점의 이웃 순회
                // - 미방문 정점이면 방문 처리 후 큐에 삽입
                // ----------------------------
                for (다음정점 in 인접목록[현재정점]) { // [S4] 인접 정점 검사 단계
                    if (방문목록[다음정점]) continue
                    방문목록[다음정점] = true // [S4][V1] 발견 즉시 방문 처리
                    큐.addLast(다음정점) // [S4][V3] 다음 탐색 대상으로 큐에 추가
                }
            }
            return
        }
        bfs(시작정점)
        // ----------------------------
        // [S_END] 결과 반환
        // ----------------------------
        return 방문순서.toIntArray()
    }
}
