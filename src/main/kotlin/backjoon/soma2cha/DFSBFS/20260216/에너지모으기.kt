package backjoon.`20260216`

/*
[문제 요약(현재 우리가 맞춘 버전)]
- 양 끝을 제외한 구슬 하나를 제거할 때마다 (왼쪽 이웃 * 오른쪽 이웃) 에너지를 얻는다.
- 구슬이 2개 남을 때까지 반복한다.
- 출력: 얻을 수 있는 최대 에너지
*/

class Solution {

    /**
     * [이 풀이의 핵심 아이디어]
     * - 가능한 제거 순서를 전부 탐색하는 백트래킹(DFS)으로 최대값을 찾는다.
     * - 선택 -> 재귀 -> 복구(undo) 패턴을 유지한다.
     *
     * [데이터 구조]
     * - 구슬목록: 현재 남아있는 구슬 상태
     * - 현재합: 현재까지 누적 에너지
     * - 최대합: 최적 답
     *
     * [시뮬레이션 순서]
     * <1> 제거 가능한 인덱스를 하나 선택
     * <2> 제거로 얻는 에너지 누적
     * <3> 재귀 탐색
     * <4> 상태 복구 후 다음 선택 진행
     */
    fun solution(구슬배열: IntArray): Int {
        // [S0] 초기화(V0~V2)
        val 구슬목록 = 구슬배열.toMutableList() // [V0]
        var 현재합 = 0 // [V1]
        var 최대합 = 0 // [V2]

        fun dfs() {
            // [S1] 종료 조건
            if (구슬목록.size == 2) {
                최대합 = maxOf(최대합, 현재합) // [S1][V2]
                return
            }

            // [S2] 제거 인덱스 순회
            for (i in 1 until 구슬목록.size - 1) {
                val left = 구슬목록[i - 1]
                val right = 구슬목록[i + 1]
                val 획득에너지 = left * right

                // [S3] 선택(제거 + 누적)
                val 제거값 = 구슬목록.removeAt(i)
                현재합 += 획득에너지 // [S3][V1]

                // [S4] 재귀 확장
                dfs() // [S4]

                // [S5] 복구(undo)
                현재합 -= 획득에너지 // [S5][V1]
                구슬목록.add(i, 제거값)
            }
        }

        dfs()

        // [S_END] 결과 반환
        return 최대합
    }
}
