package 코테.`12-9`

/*
[문제 요약(현재 우리가 맞춘 버전)]
- n개의 숫자 배열이 있고, 순서는 바꾸지 않는다.
- 각 숫자 앞에 + 또는 - 를 붙여 누적합을 만든다.
- 최종 누적합이 target과 같아지는 경우의 수를 구한다.
- 출력: target을 만드는 방법 수
numbers = [1, 1, 1, 1, 1]
target = 3
answer = 5
*/

class Solution {

    /**
     * [이 풀이의 핵심 아이디어]
     * - 각 숫자마다 선택지는 2개(+붙이기, -붙이기)다.
     * - 깊이 우선 탐색(DFS)으로 모든 선택 트리를 끝까지 탐색한다.
     * - 리프(모든 숫자 사용 완료)에서 누적합이 target이면 정답 카운트를 증가시킨다.
     *
     * [데이터 구조]
     * - 숫자목록(IntArray): 입력 숫자 배열
     * - 목표값(Int): target
     * - 방법수(Int): 정답 카운트
     * - dfs(깊이, 현재합): 현재 인덱스와 누적합 상태를 들고 내려가는 재귀 함수
     *
     * [시뮬레이션 순서]
     * <1> 입력/상태 변수 초기화
     * <2> dfs(0, 0)으로 탐색 시작
     * <3> 리프(깊이==최대깊이)면 target 일치 여부 검사
     * <4> 리프가 아니면 현재 숫자에 대해 +분기, -분기 재귀 호출
     * <5> 모든 분기 종료 후 누적된 방법수 반환
     *
     * [주석 템플릿 규칙]
     * - [S*] : 알고리즘 실행 흐름 단계
     * - [V*] : 핵심 상태 변수(데이터 저장소)
     * - 본문 진행 순서: [S0] 초기화 -> [S1..S6] DFS 처리 -> [S_END] 반환
     * - 상태 변수 선언/핵심 갱신 위치에는 [V*]를 함께 표기
     */
    fun solution(numbers: IntArray, target: Int): Int {
        // [S0] 초기화 구간 시작: 입력/상태변수(V0~V5) 준비
        val 숫자목록 = numbers // [V0] 입력 숫자 배열
        val 목표값 = target // [V1] 타겟 값
        val 최대깊이 = 숫자목록.size // [V5] 숫자 개수
        var 방법수 = 0 // [V2] 타겟 도달 방법 수

        fun dfs(깊이: Int, 현재합: Int) { // [V3] 깊이(index), [V4] 누적합
            // [S1] 종료 조건 검사: 모든 숫자를 사용했는가
            if (깊이 == 최대깊이) {
                // [S2] 리프에서 target 일치 여부 확인
                if (현재합 == 목표값) {
                    방법수++ // [S2][V2] 방법 수 1 증가
                }
                return
            }

            // [S3] 현재 깊이의 숫자 선택
            val 현재숫자 = 숫자목록[깊이]

            // [S4] +분기 탐색
            dfs(깊이 + 1, 현재합 + 현재숫자) // [S4][V3][V4]

            // [S5] -분기 탐색
            dfs(깊이 + 1, 현재합 - 현재숫자) // [S5][V3][V4]
        }

        // [S6] DFS 시작
        dfs(0, 0)

        // [S_END] 결과 반환
        return 방법수
    }
}
