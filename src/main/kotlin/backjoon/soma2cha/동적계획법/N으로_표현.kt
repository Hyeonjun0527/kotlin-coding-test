/*
[문제 요약]
- 숫자 N과 사칙연산(+,-,*,/)만 사용해 target 숫자를 표현한다.
- N 사용 횟수의 최솟값을 구하고, 8보다 크면 -1을 반환한다.
*/
class Solution {

    /**
     * [핵심 아이디어]
     * - `dp[k]`를 "N을 정확히 k번 써서 만들 수 있는 모든 정수 집합"으로 둔다.
     * - `dp[k]`는 두 방식으로 채운다.
     *   <1> 이어붙인 수: N, NN, NNN, ...
     *   <2> 분할 조합: `dp[i]`와 `dp[k-i]`의 모든 쌍에 사칙연산 적용
     * - k를 1부터 8까지 증가시키며 처음으로 target이 포함되는 k가 정답이다.
     *
     * [데이터 구조]
     * - V0 기준숫자(Int): 입력 N
     * - V1 목표값(Int): 입력 number
     * - V2 최대사용횟수(Int): 문제 제한 8
     * - V3 경우의수집합(Array<MutableSet<Int>>): 사용 횟수별 가능한 값 집합
     * - V4 사용횟수(Int): 현재 만들고 있는 N 사용 횟수
     * - V5 이어붙인수(Int): N을 연속으로 붙인 값
     * - V6 왼쪽횟수(Int): 분할의 왼쪽 사용 횟수
     * - V7 오른쪽횟수(Int): 분할의 오른쪽 사용 횟수
     * - V8 왼쪽값(Int): 경우의수집합[왼쪽횟수] 원소
     * - V9 오른쪽값(Int): 경우의수집합[오른쪽횟수] 원소
     *
     * [처리 순서]
     * <1> 기저값(N == number)을 먼저 처리한다.
     * <2> 사용횟수 1..8 순서로 dp 집합을 채운다.
     * <3> 이어붙인 수를 추가한다.
     * <4> 모든 분할(i, k-i) 조합으로 사칙연산 결과를 추가한다.
     * <5> 목표값 포함 여부를 검사하고 포함되면 즉시 반환한다.
     * <6> 끝까지 없으면 -1 반환한다.
     *
     * [주석 템플릿 규칙]
     * - [S*] : 실행 단계
     * - [V*] : 상태 변수
     * - 핵심 분기/확장 라인에는 [S4]를 명시
     */
    fun solution(N: Int, number: Int): Int {
        // [S0] 상태 초기화
        val 기준숫자 = N // [V0]
        val 목표값 = number // [V1]
        val 최대사용횟수 = 8 // [V2]
        val 경우의수집합 = Array(최대사용횟수 + 1) { mutableSetOf<Int>() } // [V3]

        // [S1] 기저값 검사
        if (기준숫자 == 목표값) return 1 // [S1][V0][V1]

        // [S2] 사용 횟수 1..8 순회
        for (사용횟수 in 1..최대사용횟수) { // [V4]
            var 이어붙인수 = 0 // [V5]
            repeat(사용횟수) { 이어붙인수 = 이어붙인수 * 10 + 기준숫자 }
            경우의수집합[사용횟수].add(이어붙인수) // [S2][V3][V4][V5]

            // [S3] 분할 조합 순회
            for (왼쪽횟수 in 1 until 사용횟수) { // [V6]
                val 오른쪽횟수 = 사용횟수 - 왼쪽횟수 // [V7]

                // [S4] 핵심 확장: 왼쪽/오른쪽 집합의 모든 쌍 사칙연산
                for (왼쪽값 in 경우의수집합[왼쪽횟수]) { // [V8]
                    for (오른쪽값 in 경우의수집합[오른쪽횟수]) { // [V9]
                        경우의수집합[사용횟수].add(왼쪽값 + 오른쪽값) // [S4][V3][V4][V8][V9]
                        경우의수집합[사용횟수].add(왼쪽값 - 오른쪽값) // [S4][V3][V4][V8][V9]
                        경우의수집합[사용횟수].add(왼쪽값 * 오른쪽값) // [S4][V3][V4][V8][V9]
                        if (오른쪽값 != 0) { // [S4][V9]
                            경우의수집합[사용횟수].add(왼쪽값 / 오른쪽값) // [S4][V3][V4][V8][V9]
                        }
                    }
                }
            }

            // [S5] 목표값 도달 검사
            if (목표값 in 경우의수집합[사용횟수]) return 사용횟수 // [S5][V1][V3][V4]
        }

        // [S_END] 제한 횟수 내 불가능
        return -1
    }
}
