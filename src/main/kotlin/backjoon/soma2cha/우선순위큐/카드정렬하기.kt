package backjoon.soma2cha.우선순위큐// BOJ: https://www.acmicpc.net/problem/1715
import java.util.PriorityQueue

/*
[문제 요약]
- 카드 묶음들을 하나로 합칠 때 필요한 최소 비교 횟수를 구한다.
- 한 번에 두 묶음을 합칠 수 있고, 비용은 두 묶음 크기의 합이다.
*/

class Solution {

    /**
     * [핵심 아이디어]
     * - 항상 가장 작은 두 묶음을 먼저 합쳐야 총합이 최소가 된다.
     * - 최소 힙(PriorityQueue)을 사용하면 작은 묶음을 빠르게 꺼낼 수 있다.
     *
     * [데이터 구조]
     * - V0 묶음수(Int): 카드 묶음 개수
     * - V1 카드힙(PriorityQueue<Int>): 카드 묶음 최소 힙
     * - V2 총비용(Int): 누적 비교 횟수
     * - V3 묶음A(Int): 첫 번째로 꺼낸 묶음
     * - V4 묶음B(Int): 두 번째로 꺼낸 묶음
     * - V5 합친묶음(Int): 합쳐진 묶음 크기
     *
     * [처리 순서]
     * <1> 카드 힙 초기화
     * <2> 묶음이 1개 이하이면 0 반환
     * <3> 힙에서 가장 작은 두 묶음 추출
     * <4> 두 묶음을 합치고 비용 누적 후 다시 힙에 삽입
     * <5> 힙에 1개만 남으면 종료
     *
     * [주석 템플릿 규칙]
     * - [S*] : 실행 단계
     * - [V*] : 상태 변수
     * - 핵심 분기/확장 라인에는 [S4]를 명시
     */
    fun solution(카드묶음목록: IntArray): Int {
        // [S0] 상태 초기화
        val 묶음수 = 카드묶음목록.size // [V0]
        val 카드힙 = PriorityQueue<Int>() // [V1]
        for (카드묶음 in 카드묶음목록) 카드힙.add(카드묶음)
        var 총비용 = 0 // [V2]

        // [S1] 조기 종료 조건
        if (묶음수 <= 1) return 0 // [S1][V0]

        // [S2] 힙에서 2개씩 꺼내 합치기
        while (카드힙.size > 1) {
            // [S3] 최소 2묶음 추출
            val 묶음A = 카드힙.poll() // [V3]
            val 묶음B = 카드힙.poll() // [V4]

            // [S4] 핵심 갱신: 합치고 누적
            val 합친묶음 = 묶음A + 묶음B // [V5]
            총비용 += 합친묶음 // [S4][V2][V5]
            카드힙.add(합친묶음) // [S4][V1][V5]
        }

        // [S_END] 결과 반환
        return 총비용
    }
}
