package backjoon.soma2cha.우선순위큐// BOJ: https://www.acmicpc.net/problem/1202

import java.util.PriorityQueue

/*
[문제 요약]
- 무게/가치가 있는 보석 N개와 최대 허용 무게가 있는 가방 K개가 주어진다.
- 각 가방에는 보석을 최대 1개만 담을 수 있을 때, 훔칠 수 있는 보석 가치 합의 최댓값을 구한다.

예시
| N,K | 보석목록(M,V) | 가방목록(C) | return |
|---|---|---|---|
| 3,2 | [[1,65],[5,23],[2,99]] | [10,2] | 164 |

의미
- N,K: 보석/가방 개수
- 보석목록: 각 원소는 [무게, 가치]
- 가방목록: 각 원소는 가방 최대 허용 무게
- return: 가능한 최대 가치 합
*/

class Solution {

    /**
     * [핵심 아이디어]
     * - 가방을 작은 용량부터 처리하면, 현재 가방에 "들어갈 수 있는 보석 후보"를 점진적으로 관리할 수 있다.
     * - 보석은 무게 오름차순으로 정렬하고,
     *   현재 가방 용량 이하인 보석의 가치만 최대 힙에 넣는다.
     * - 각 가방에서는 힙의 최댓값(가장 비싼 보석) 하나를 선택하면 전체 합이 최대가 된다.
     *
     * [데이터 구조]
     * - V0 보석개수(Int): 전체 보석 수
     * - V1 가방개수(Int): 전체 가방 수
     * - V2 정렬보석목록(List<IntArray>): 무게 오름차순 보석 목록
     * - V3 정렬가방목록(List<Int>): 용량 오름차순 가방 목록
     * - V4 가치힙(PriorityQueue<Int>): 현재 선택 가능한 보석 가치 최대 힙
     * - V5 보석인덱스(Int): 다음으로 힙에 넣을 보석 인덱스
     * - V6 누적가치합(Long): 현재까지 선택한 보석 가치 합
     * - V7 현재가방용량(Int): 현재 처리 중인 가방 용량
     * - V8 현재보석(IntArray): 현재 힙 후보로 확인 중인 보석
     *
     * [처리 순서]
     * <1> 보석/가방 정렬
     * <2> 가방을 작은 용량부터 순회
     * <3> 현재 가방에 담을 수 있는 보석 가치들을 힙에 추가
     * <4> 힙에서 가장 큰 가치 보석 하나 선택
     * <5> 모든 가방 처리 후 누적가치합 반환
     *
     * [주석 템플릿 규칙]
     * - [S*] : 실행 단계
     * - [V*] : 상태 변수
     * - 핵심 분기/확장 라인에는 [S4]를 명시
     */
    fun solution(보석목록: Array<IntArray>, 가방목록: IntArray): Long {
        // [S0] 상태 초기화
        val 보석개수 = 보석목록.size // [V0]
        val 가방개수 = 가방목록.size // [V1]
        if (보석개수 == 0 || 가방개수 == 0) return 0L // [S0][V0][V1]

        // [S1] 정렬
        val 정렬보석목록 = 보석목록//보석 : 무게,가치
            .sortedBy { it[0] } // [V2]
        val 정렬가방목록 = 가방목록
            .sorted() // [V3]

        // [S2] 힙/포인터/정답 초기화
        val 가치힙 = PriorityQueue<Int>(compareByDescending { it }) // [V4]
        var 보석인덱스 = 0 // [V5]
        var 누적가치합 = 0L // [V6]

        // [S3] 가방 순회
        for (현재가방용량 in 정렬가방목록) { // [V7]
            // [S4] 핵심 확장: 현재 가방에 담을 수 있는 보석 가치들을 힙에 적재
            while (보석인덱스 < 보석개수) {
                val 현재보석 = 정렬보석목록[보석인덱스] // [V8]
                if (현재보석[0] > 현재가방용량) break//무게. 보석 무게가 더 커지면 스탑.
                가치힙.add(현재보석[1]) // [S4][V4][V8]//가치
                보석인덱스++ // [S4][V5]
            }

            // [S5] 현재 가방에서 최고 가치 보석 선택
            if (가치힙.isNotEmpty()) {
                누적가치합 += 가치힙.poll().toLong() // [S5][V4][V6]
            }
        }

        // [S_END] 결과 반환
        return 누적가치합
    }
}
