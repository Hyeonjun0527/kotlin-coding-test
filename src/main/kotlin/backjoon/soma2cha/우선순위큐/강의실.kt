package backjoon.soma2cha.우선순위큐// BOJ: https://www.acmicpc.net/problem/1374
import java.util.PriorityQueue

/*
[문제 요약]
- 강의번호/시작/종료 정보가 주어질 때, 모든 강의를 진행하기 위한 최소 강의실 수를 구한다.
- 어떤 강의의 종료시간과 다른 강의의 시작시간이 같으면 같은 강의실을 재사용할 수 있다.

예시
| N | 강의목록 | return |
|---|---|---|
| 8 | [[6,15,21],[7,20,25],[1,3,8],[3,2,14],[8,6,27],[2,7,13],[4,12,18],[5,6,20]] | 5 |

의미
- N: 강의 개수
- 강의목록: 각 원소는 [강의번호, 시작시간, 종료시간]
- return: 모든 강의를 배치하기 위해 필요한 최소 강의실 수
*/

class Solution {

    /**
     * [핵심 아이디어]
     * - 시작시간이 빠른 순서로 강의를 처리한다.
     * - 현재 사용 중인 강의실들의 "종료시간"만 최소 힙으로 관리하면 재사용 여부를 O(logN)에 판단할 수 있다.
     * - 가장 빨리 끝나는 강의실의 종료시간이 현재 시작시간 이하라면 그 강의실을 재사용한다.
     *
     * [데이터 구조]
     * - V0 강의개수(Int): 전체 강의 수
     * - V1 정렬강의목록(List<IntArray>): 시작시간 기준 정렬된 강의 목록
     * - V2 종료시간힙(PriorityQueue<Int>): 사용 중 강의실 종료시간 최소 힙
     * - V3 강의(IntArray): 현재 처리 강의 [강의번호, 시작시간, 종료시간]
     * - V4 시작시간(Int): 현재 강의 시작시간
     * - V5 종료시간(Int): 현재 강의 종료시간
     *
     * [처리 순서]
     * <1> 시작시간 기준 정렬
     * <2> 강의를 순서대로 하나씩 처리
     * <3> 재사용 가능한 강의실이 있으면 종료시간 힙에서 제거
     * <4> 현재 강의 종료시간을 힙에 삽입
     * <5> 힙 크기를 최소 강의실 수로 반환
     *
     * [주석 템플릿 규칙]
     * - [S*] : 실행 단계
     * - [V*] : 상태 변수
     * - 핵심 분기/확장 라인에는 [S4]를 명시
     */
    fun solution(강의목록: Array<IntArray>): Int {
        // [S0] 상태 초기화
        val 강의개수 = 강의목록.size // [V0]
        if (강의개수 == 0) return 0 // [S0][V0]

        // [S1] 시작시간(동일 시 종료시간) 기준 정렬
        val 정렬강의목록 = 강의목록
            .sortedWith(compareBy<IntArray> { it[1] }.thenBy { it[2] }) // [V1]

        // [S2] 종료시간 최소 힙 초기화
        val 종료시간힙 = PriorityQueue<Int>() // [V2]

        // [S3] 강의 순회
        for (강의 in 정렬강의목록) { // [V3]
            val 시작시간 = 강의[1] // [V4]
            val 종료시간 = 강의[2] // [V5]

            // [S4] 핵심 분기: 가장 빨리 끝나는 강의실 재사용 가능 여부
            if (종료시간힙.isNotEmpty() && 종료시간힙.peek() <= 시작시간) { // [S4][V2][V4]
                종료시간힙.poll() // [S4][V2]
            }

            // [S5] 현재 강의 종료시간 반영
            종료시간힙.add(종료시간) // [S5][V2][V5]
        }

        // [S_END] 최소 강의실 수 반환
        return 종료시간힙.size
    }
}
