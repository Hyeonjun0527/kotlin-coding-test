// BOJ: https://www.acmicpc.net/problem/19598
import java.util.PriorityQueue

/*
[문제 요약]
- 시작/종료 시간이 주어진 모든 회의를 중단 없이 진행하려면 최소 몇 개의 회의실이 필요한지 구한다.
- 어떤 회의가 끝나는 시각과 다음 회의 시작 시각이 같으면 같은 회의실을 재사용할 수 있다.

예시
| N | 회의목록 | return |
|---|---|---|
| 3 | [[0,40],[15,30],[5,10]] | 2 |

의미
- N: 회의 개수
- 회의목록: 각 원소는 [시작시간, 종료시간]
- return: 모든 회의를 배치하기 위한 최소 회의실 개수
*/

class Solution {

    /**
     * [핵심 아이디어]
     * - 시작시간이 빠른 순으로 회의를 처리한다.
     * - 현재 사용 중인 회의실들의 종료시간을 최소 힙으로 유지하면,
     *   가장 빨리 비는 회의실이 지금 회의를 받을 수 있는지 즉시 판별할 수 있다.
     *
     * [데이터 구조]
     * - V0 회의개수(Int): 전체 회의 수
     * - V1 정렬회의목록(List<IntArray>): 시작시간 기준 정렬된 회의 목록
     * - V2 종료시간힙(PriorityQueue<Int>): 사용 중 회의실의 종료시간 최소 힙
     * - V3 회의(IntArray): 현재 처리 회의 [시작시간, 종료시간]
     * - V4 시작시간(Int): 현재 회의 시작시간
     * - V5 종료시간(Int): 현재 회의 종료시간
     *
     * [처리 순서]
     * <1> 회의 목록 정렬
     * <2> 정렬된 회의를 앞에서부터 순회
     * <3> 재사용 가능한 회의실이 있으면 힙에서 제거
     * <4> 현재 회의 종료시간을 힙에 삽입
     * <5> 힙 크기를 최소 회의실 개수로 반환
     *
     * [주석 템플릿 규칙]
     * - [S*] : 실행 단계
     * - [V*] : 상태 변수
     * - 핵심 분기/확장 라인에는 [S4]를 명시
     */
    fun solution(회의목록: Array<IntArray>): Int {
        // [S0] 상태 초기화
        val 회의개수 = 회의목록.size // [V0]
        if (회의개수 == 0) return 0 // [S0][V0]

        // [S1] 시작시간(동일 시 종료시간) 기준 정렬
        val 정렬회의목록 = 회의목록
            .sortedWith(compareBy<IntArray> { it[0] }.thenBy { it[1] }) // [V1]

        // [S2] 종료시간 최소 힙 초기화
        val 종료시간힙 = PriorityQueue<Int>() // [V2]

        // [S3] 회의 순회
        for (회의 in 정렬회의목록) { // [V3]
            val 시작시간 = 회의[0] // [V4]
            val 종료시간 = 회의[1] // [V5]

            // [S4] 핵심 분기: 가장 빨리 끝나는 회의실 재사용 가능 여부
            if (종료시간힙.isNotEmpty() && 종료시간힙.peek() <= 시작시간) { // [S4][V2][V4]
                종료시간힙.poll() // [S4][V2]
            }

            // [S5] 현재 회의 종료시간 반영
            종료시간힙.add(종료시간) // [S5][V2][V5]
        }

        // [S_END] 최소 회의실 개수 반환
        return 종료시간힙.size
    }
}
