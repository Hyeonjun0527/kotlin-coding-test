package backjoon.soma2cha.우선순위큐// BOJ: https://www.acmicpc.net/problem/12764
import java.util.PriorityQueue

/*
[문제 요약]
- 사용자별 이용 시작/종료 시각이 주어질 때 최소 좌석 개수로 모두 배치한다.
- 사용 종료 시각과 다음 사용 시작 시각이 같으면 같은 좌석을 바로 재사용할 수 있다.
- 최소 좌석 개수와 좌석별 사용 횟수를 좌석 번호 오름차순으로 구한다.

예시
| N | 이용목록 | return |
|---|---|---|
| 3 | [[1,4],[2,3],[4,5]] | [2, [2,1]] |

의미
- N: 사용자 수
- 이용목록: 각 원소는 [시작시각, 종료시각]
- return: 첫 값은 총 좌석 수, 두 번째 값은 좌석번호 순 사용횟수 배열
*/

class Solution {

    /**
     * [핵심 아이디어]
     * - 시작 시각 순으로 시뮬레이션하면 "현재 시점에 비는 좌석"을 정확히 판단할 수 있다.
     * - 두 개의 힙을 사용한다.
     *   - 사용중자리힙: (종료시각, 자리번호) 최소 힙
     *   - 빈자리힙: 비어 있는 자리번호 최소 힙
     * - 항상 가장 작은 빈 자리번호를 재사용하고, 없으면 새 자리를 만든다.
     *
     * [데이터 구조]
     * - V0 사용자수(Int): 전체 사용자 수
     * - V1 정렬이용목록(List<IntArray>): 시작시각 기준 정렬된 이용 목록
     * - V2 사용중자리힙(PriorityQueue<Pair<Int, Int>>): (종료시각, 자리번호) 최소 힙
     * - V3 빈자리힙(PriorityQueue<Int>): 사용 가능한 자리번호 최소 힙
     * - V4 자리사용횟수(MutableList<Int>): 자리번호별 누적 사용 횟수
     * - V5 총자리수(Int): 생성된 전체 자리 수
     * - V6 이용정보(IntArray): 현재 처리 이용정보 [시작시각, 종료시각]
     * - V7 시작시각(Int): 현재 시작시각
     * - V8 종료시각(Int): 현재 종료시각
     * - V9 비워진자리번호(Int): 반납된 자리번호
     * - V10 배정자리번호(Int): 현재 사용자에게 배정한 자리번호
     *
     * [처리 순서]
     * <1> 시작시각 기준 정렬
     * <2> 각 사용자 시작 전에 이미 끝난 좌석들을 모두 반납
     * <3> 빈자리가 있으면 가장 작은 번호 재사용, 없으면 새 자리 생성
     * <4> 배정 좌석 사용횟수 증가 및 사용중 힙 등록
     * <5> 총자리수와 사용횟수 배열 반환
     *
     * [주석 템플릿 규칙]
     * - [S*] : 실행 단계
     * - [V*] : 상태 변수
     * - 핵심 분기/확장 라인에는 [S4]를 명시
     */
    fun solution(이용목록: Array<IntArray>): Pair<Int, IntArray> {
        // [S0] 상태 초기화
        val 사용자수 = 이용목록.size // [V0]
        if (사용자수 == 0) return 0 to intArrayOf() // [S0][V0]

        // [S1] 시작시각(동일 시 종료시각) 기준 정렬
        // 0 : 시작시각 , 1 : 종료시각 기준 정렬. 시간 시뮬레이션 하기 위함.
        val 정렬이용목록 = 이용목록
            .sortedWith(compareBy<IntArray> { it[0] }.thenBy { it[1] }) // [V1]

        // [S2] 힙/카운터 초기화
        //사용중자리힙 : (종료시각, 자리번호)
        val 사용중자리힙 = PriorityQueue<Pair<Int, Int>>(
            compareBy<Pair<Int, Int>> { it.first }.thenBy { it.second }
        ) // [V2]
        //빈자리힙 :빈자리번호 관리하는 힙
        val 빈자리힙 = PriorityQueue<Int>() // [V3]
        //자리사용횟수 : 자리번호별 누적 사용 횟수
        val 자리별사용횟수 = mutableListOf<Int>() // [V4]
        //총자리수 : 생성된 전체 자리 수
        var 총자리수 = 0 // [V5]

        // [S3] 사용자 순회
        for (이용정보 in 정렬이용목록) { // [V6]
            val 시작시각 = 이용정보[0] // [V7]
            val 종료시각 = 이용정보[1] // [V8]

            // [S4] 핵심 확장: 시작 전까지 종료된 좌석 모두 반납
            while (사용중자리힙.isNotEmpty() && 사용중자리힙.peek().first <= 시작시각) { // [S4][V2][V7]
                val 비워진자리번호 = 사용중자리힙.poll().second // [V9]
                빈자리힙.add(비워진자리번호) // [S4][V3][V9]
            }

            // [S5] 좌석 배정
            val 배정자리번호 = if (빈자리힙.isNotEmpty()) {//빈자리있으면 빼서 줌.(이전에 while로 사용중자리힙에서 비어있는 좌석 모두 반납해서 빈자리힙에 넣어놓음)
                빈자리힙.poll()
            } else { //빈자리없으면 하나 만들어주고.
                총자리수 += 1 // [V5]
                자리별사용횟수.add(0) // [V4]
                총자리수
            } // [V10]

            // [S6] 사용 횟수/사용중 등록
            자리별사용횟수[배정자리번호 - 1] += 1 // [V4][V10]
            사용중자리힙.add(종료시각 to 배정자리번호) // [V2][V8][V10]
        }

        // [S_END] 결과 반환
        return 총자리수 to 자리별사용횟수.toIntArray()
    }
}


/*
이용목록 = [[10,40],[20,30],[40,50]]
[1,4] : 사람1이 10시각 ~ 40시각 동안 사용
전체상태표
| 단계  | 현재 처리 사람     | 한 일                | 사용중자리힙(종료시각,자리번호)        | 빈자리힙 | 자리별사용횟수 | 총자리수 |
| --- | ------------ | ----------------------- | ------------------------------   | ------- | --------- | ---- |
| 0   | 시작 전         | 초기 상태                   | `[]`                         | `[]`    | `[]`    | `0`  |
| 1   | `[10,40]`    | 빈 자리 없어서 1번 생성 후 배정     | `[(40,1)]`                | `[]`    | `[1]`   | `1`  |
| 2   | `[20,30]`    | 아직 끝난 자리 없음, 2번 생성 후 배정 | `[(30,2),(40,1)]`        | `[]`    | `[1,1]` | `2`  |
| 3-1 | `[40,50]` 직전 | 30에 끝난 2번 자리 반납         | `[(40,1)]`                 | `[2]`   | `[1,1]` | `2`  |
| 3-2 | `[40,50]` 직전 | 40에 끝난 1번 자리도 반납        | `[]`                       | `[1,2]` | `[1,1]` | `2`  |
| 3-3 | `[40,50]`    | 빈 자리 중 가장 작은 1번 배정      | `[(50,1)]`                 | `[2]`   | `[2,1]` | `2`  |

* */