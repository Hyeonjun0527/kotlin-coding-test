package backjoon.soma2cha.우선순위큐// BOJ: https://www.acmicpc.net/problem/1655
import java.util.PriorityQueue

/*
[문제 요약]
- 수를 하나씩 받을 때마다 지금까지의 중간값을 즉시 구한다.
- 입력 개수가 짝수면 가운데 두 값 중 작은 값을 중간값으로 사용한다.

예시
| N | 숫자목록 | return |
|---|---|---|
| 7 | [1,5,2,10,-99,7,5] | [1,1,2,2,2,2,5] |

의미
- N: 입력되는 정수 개수
- 숫자목록: 순서대로 들어오는 정수 스트림
- return: 각 단계에서 동생이 말해야 하는 중간값 목록
*/

class Solution89 {

    /**
     * [핵심 아이디어]
     * - 작은 절반은 최대 힙, 큰 절반은 최소 힙으로 유지한다.
     * - 작은 절반의 크기를 큰 절반보다 항상 같거나 1 크게 맞춘다.
     * - 그러면 현재 중간값(짝수일 때는 작은 쪽 중간값)은 항상 작은 절반 힙의 top이다.
     *
     * [데이터 구조]
     * - V0 입력개수(Int): 전체 입력 수
     * - V1 작은절반힙(PriorityQueue<Int>): 최대 힙
     * - V2 큰절반힙(PriorityQueue<Int>): 최소 힙
     * - V3 중간값목록(MutableList<Int>): 단계별 중간값 결과
     * - V4 현재수(Int): 현재 처리할 입력 값
     * - V5 작은힙Top(Int): swap 직전 작은절반힙 top
     * - V6 큰힙Top(Int): swap 직전 큰절반힙 top
     *
     * [처리 순서]
     * <1> 두 힙/정답 목록 초기화
     * <2> 숫자를 앞에서부터 하나씩 처리
     * <3> 힙 크기 규칙에 따라 현재 숫자 삽입
     * <4> 두 힙 top 순서가 뒤집혔으면 swap으로 복구
     * <5> 작은절반힙 top을 정답에 기록
     *
     * [주석 템플릿 규칙]
     * - [S*] : 실행 단계
     * - [V*] : 상태 변수
     * - 핵심 분기/확장 라인에는 [S4]를 명시
     */
    fun solution(숫자목록: IntArray): IntArray {
        // [S0] 상태 초기화
        val 입력개수 = 숫자목록.size // [V0]
        if (입력개수 == 0) return intArrayOf() // [S0][V0]

        val 작은절반힙 = PriorityQueue<Int>(compareByDescending { it }) // [V1]
        val 큰절반힙 = PriorityQueue<Int>() // [V2]
        val 중간값목록 = mutableListOf<Int>() // [V3]

        // [S1] 숫자 순회
        for (현재수 in 숫자목록) { // [V4]
            // [S2] 크기 균형 규칙으로 삽입
            if (작은절반힙.size == 큰절반힙.size) {
                작은절반힙.add(현재수) // [V1][V4]
            } else {
                큰절반힙.add(현재수) // [V2][V4]
            }

            // [S3] top 순서 점검
            if (큰절반힙.isNotEmpty() && 작은절반힙.peek() > 큰절반힙.peek()) {
                // [S4] 핵심 갱신: top swap으로 순서 복구
                val 작은힙Top = 작은절반힙.poll() // [V5]
                val 큰힙Top = 큰절반힙.poll() // [V6]
                작은절반힙.add(큰힙Top) // [S4][V1][V6]
                큰절반힙.add(작은힙Top) // [S4][V2][V5]
            }

            // [S5] 현재 중간값 기록
            중간값목록.add(작은절반힙.peek()) // [S5][V1][V3]
        }

        // [S_END] 결과 반환
        return 중간값목록.toIntArray()
    }
}
/*
정답이 항상 작은절반힙의 top 이 되게한다.
그러려면 작은절반힙 쪽에 가운데 값을 포함시키는 구조여야 해.
그래서 개수를 이렇게 맞추는 거임.
홀수 개일 때 → 작은절반힙이 1개 더 많음
짝수 개일 때 → 두 힙 개수가 같음
그래서 서로 크기 같으면 작은절반힙에 하나 더 넣음. 이게 홀수개일때임.
두 힙 크기 다르면 왜 큰절반힙에 하나 넣지? 두 힙크기가 다른 경우는 언제나 작은절반힙이 하나 더 많은 생태이기때문.
---


이 문제는 숫자가 하나씩 들어올 때마다 그 순간의 중간값을 바로 말해야 하는 문제다. 여기서 중요한 점은, 마지막에 한 번만 중간값을 구하는 것이 아니라, 숫자가 들어올 때마다 매번 새로운 중간값을 구해야 한다는 것이다. 그래서 숫자를 하나 받을 때마다 상태를 바로바로 관리할 수 있는 방법이 필요하다.

중간값은 숫자들을 정렬했을 때 한가운데에 있는 값이다. 그런데 매번 숫자가 하나 들어올 때마다 전체를 다시 정렬하면 너무 느리다. 그래서 이 문제는 전체를 한 줄로 다시 세우는 대신, 숫자들을 두 무리로 나눠서 관리한다. 하나는 작은 값들이 모여 있는 왼쪽 무리이고, 다른 하나는 큰 값들이 모여 있는 오른쪽 무리이다. 코드에서 작은절반힙은 왼쪽 무리이고, 큰절반힙은 오른쪽 무리이다.

이 두 무리를 만들 때 반드시 지켜야 하는 규칙이 두 개 있다. 첫 번째 규칙은 개수 규칙이다. 왼쪽 무리의 개수는 오른쪽 무리의 개수와 같거나, 딱 1개 더 많아야 한다. 이 규칙을 지키는 이유는 정답을 항상 왼쪽 무리에서 꺼내기 위해서다. 숫자가 홀수 개이면 가운데 값이 하나인데, 그 값이 왼쪽 무리 맨 끝에 오게 만들고 싶다. 숫자가 짝수 개이면 가운데 값이 두 개인데, 문제에서는 그중 더 작은 값을 답으로 써야 하므로 역시 왼쪽 무리 맨 끝값이 답이 된다. 그래서 왼쪽 무리가 항상 같거나 1개 더 많게 유지된다.

두 번째 규칙은 값 규칙이다. 왼쪽 무리에 들어 있는 모든 값은 오른쪽 무리에 들어 있는 모든 값보다 작거나 같아야 한다. 그래야만 왼쪽 무리가 진짜 “작은 값들”, 오른쪽 무리가 진짜 “큰 값들”이 된다. 만약 왼쪽 무리에 너무 큰 값이 들어가 있고, 오른쪽 무리에 너무 작은 값이 들어가 있으면, 두 무리는 이름만 왼쪽과 오른쪽이지 실제로는 잘못 나뉜 상태가 된다. 그 상태에서는 왼쪽 맨 끝값을 중간값이라고 믿을 수 없다.

그래서 숫자 하나를 처리할 때는 항상 두 단계를 거친다. 먼저 개수 규칙을 맞추기 위해 숫자를 넣는다. 두 힙의 크기가 같으면 왼쪽 힙에 넣고, 왼쪽 힙이 하나 더 많으면 오른쪽 힙에 넣는다. 이렇게 하면 개수 균형은 항상 맞는다. 여기서 중요한 것은, 이 단계에서는 아직 숫자의 크기를 깊게 따지지 않는다는 점이다. 일단 이번 차례에 왼쪽에 넣을지 오른쪽에 넣을지만 정하는 것이다.

그 다음에는 값 규칙이 깨졌는지 확인한다. 이때 전체 값을 다 비교할 필요는 없다. 왜냐하면 문제가 생길 수 있는 곳은 경계뿐이기 때문이다. 왼쪽 무리에서 가장 큰 값과 오른쪽 무리에서 가장 작은 값만 보면 된다. 코드에서 작은절반힙.peek()는 왼쪽 무리의 최댓값이고, 큰절반힙.peek()는 오른쪽 무리의 최솟값이다. 정상이라면 왼쪽 최댓값이 오른쪽 최솟값보다 작거나 같아야 한다. 그런데 만약 왼쪽 최댓값이 더 크다면, 경계가 뒤집힌 것이다. 즉 왼쪽에 있어야 할 것보다 큰 값이 왼쪽에 있고, 오른쪽에 있어야 할 것보다 작은 값이 오른쪽에 있다는 뜻이다.

이 경우에는 두 힙의 맨 위 값만 서로 바꾸면 된다. 왼쪽에서 제일 큰 값을 꺼내고, 오른쪽에서 제일 작은 값을 꺼내서 자리를 바꾸는 것이다. 이렇게 하면 다시 왼쪽에는 작은 값들만, 오른쪽에는 큰 값들만 있게 된다. 왜 맨 위 둘만 바꾸면 되냐면, 새 숫자 하나가 들어오기 전까지는 이미 상태가 올바르게 유지되고 있었다고 보기 때문이다. 그러므로 새 숫자 때문에 문제가 생겼다면 전체가 다 어그러진 것이 아니라 경계만 어긋난 것이다. 그래서 경계에 있는 값 둘만 교환하면 충분하다.

이제 상태가 복구되면 정답은 항상 작은절반힙.peek()가 된다. 왼쪽 무리는 개수상으로 같거나 하나 더 많게 유지되고 있고, 값들도 오른쪽보다 작거나 같게 정리되어 있으므로, 왼쪽 무리의 맨 끝값이 바로 현재까지의 중간값이다. 홀수 개일 때는 진짜 가운데 값이고, 짝수 개일 때는 가운데 두 값 중 더 작은 값이다.

큰절반힙.isNotEmpty()를 검사하는 이유도 단순하다. 처음 숫자를 한두 개 넣는 초반에는 오른쪽 힙이 비어 있을 수 있다. 그런데 오른쪽 힙이 비어 있는 상태에서 큰절반힙.peek()를 하면 비교할 값이 없어서 문제가 생긴다. 그래서 오른쪽 힙에 값이 있을 때만 두 힙의 맨 위를 비교하는 것이다. 쉽게 말해, 오른쪽 무리가 아직 비어 있으면 “경계 비교” 자체를 할 수 없으니 먼저 비어 있지 않은지 확인하는 것이다.

이걸 아주 짧게 정리하면 이렇다. 이 문제는 숫자를 하나 받을 때마다, 먼저 왼쪽과 오른쪽의 개수 균형을 맞추고, 그 다음 왼쪽과 오른쪽의 값 순서가 맞는지 확인해서 필요하면 맨 위 둘을 바꾼다. 그러면 항상 왼쪽 맨 위가 중간값이 된다. 결국 이 알고리즘은 “전체 정렬”을 매번 하는 것이 아니라, “가운데를 기준으로 왼쪽 절반과 오른쪽 절반을 항상 올바르게 유지하는 것”만 하고 있는 것이다.

네 말로 다시 딱 바꾸면 이렇게 정리할 수 있다.

“답이 되려면 먼저 왼쪽 힙이 오른쪽 힙보다 개수가 같거나 1개 더 많아야 한다. 그리고 오른쪽 힙에 있는 값들이 왼쪽 힙에 있는 값들보다 더 작아지면 안 된다. 그래서 숫자를 하나 넣은 뒤, 왼쪽의 제일 큰 값과 오른쪽의 제일 작은 값을 비교해서 순서가 뒤집혔는지 확인하고, 뒤집혔으면 둘을 바꿔 준다. 그러면 항상 왼쪽 힙의 top이 중간값이 된다.”
 */