package backjoon.soma2cha.우선순위큐// BOJ: https://www.acmicpc.net/problem/11000
import java.util.PriorityQueue

/*
[문제 요약]
- 시작/종료 시간이 있는 수업들을 모두 배치할 때 필요한 최소 강의실 개수를 구한다.
- 어떤 수업이 끝나는 시간과 다음 수업 시작 시간이 같으면 같은 강의실을 재사용할 수 있다.

예시
| N | 수업목록 | return |
|---|---|---|
| 3 | [[1,3],[2,4],[3,5]] | 2 |

의미
- N: 수업 개수
- 수업목록: 각 원소는 [시작시간, 종료시간]
- return: 모든 수업을 배치하기 위한 최소 강의실 수
*/

class Solution {

    /**
     * [핵심 아이디어]
     * - 시작 시간이 빠른 순으로 수업을 처리한다.
     * - "현재 사용 중인 강의실들 중 가장 빨리 끝나는 시간"만 알면 재사용 여부를 즉시 판단할 수 있다.
     * - 최소 힙(PriorityQueue)에 각 강의실의 종료 시간을 넣고 관리한다.
     *
     * 이 문제는 여러 수업을 가능한 적은 개수의 강의실에 배치하는 문제이다.
     * 즉, 목표는 모든 수업을 넣되 강의실 개수는 최소로 유지하는 것이다.
     *
     * 이를 위해서는 수업을 하나씩 보면서 매번
     * “이 수업이 시작하는 시각에, 기존 강의실 중 재사용할 수 있는 강의실이 있는가? 없으면 새 강의실이 필요한가?”
     * 를 판단해야 한다.
     *
     * 이 판단이 올바르려면 수업을 시간의 흐름 순서대로 처리해야 한다.
     * 그래서 시작시간이 빠른 수업부터 정렬해서 처리한다.
     * 그래야 현재 수업보다 먼저 시작한 수업들이 이미 반영된 상태에서, 지금 시점에 비어 있는 강의실이 있는지를 자연스럽게 판단할 수 있다.
     *
     * 한편, 종료시간 정렬은 핵심 이유가 “빨리 끝나면 빨리 재사용 가능해서”는 아니다.
     * 작시간이 같은 수업들 사이의 처리 순서를 일정하게 맞추기 위함.
     *
     * [데이터 구조]
     * - V0 수업개수(Int): 전체 수업 수
     * - V1 정렬수업목록(List<IntArray>): 시작시간 기준 정렬된 수업 목록
     * - V2 종료시간힙(PriorityQueue<Int>): 현재 강의실 종료 시간 최소 힙
     * - V3 수업(IntArray): 현재 처리 수업
     * - V4 시작시간(Int): 현재 수업 시작시간
     * - V5 종료시간(Int): 현재 수업 종료시간
     *
     * [처리 순서]
     * <1> 수업 목록 정렬
     * <2> 정렬된 수업을 순서대로 처리
     * <3> 가장 빨리 끝나는 강의실이 재사용 가능하면 힙에서 제거
     * <4> 현재 수업 종료시간을 힙에 삽입
     * <5> 힙 크기를 최소 강의실 수로 반환
     *
     * [주석 템플릿 규칙]
     * - [S*] : 실행 단계
     * - [V*] : 상태 변수
     * - 핵심 분기/확장 라인에는 [S4]를 명시
     */
    fun solution(수업목록: Array<IntArray>): Int {
        // [S0] 상태 초기화
        val 수업개수 = 수업목록.size // [V0]
        if (수업개수 == 0) return 0 // [S0][V0]

        // [S1] 시작시간(동일 시 종료시간) 기준 정렬
        val 정렬수업목록 = 수업목록
            .sortedWith(compareBy<IntArray> { it[0] }.thenBy { it[1] }) // [V1]

        // [S2] 종료시간 최소 힙 초기화
        val 종료시간힙 = PriorityQueue<Int>() // [V2]

        // [S3] 수업 순회
        for (수업 in 정렬수업목록) { // [V3]
            val 시작시간 = 수업[0] // [V4]
            val 종료시간 = 수업[1] // [V5]

            // [S4] 핵심 분기: 가장 빨리 끝난 강의실 재사용 가능 여부
            //[3,4] : 강의실 2개 사용 중 하나는 3시에 비고 하나는 4시에 빈다
            //peek을 한게 사용하고있는 가장 빨리 끝나는 강의실이다. 그 강의실 종료시간이랑, 한 수업의 시작시간 비교해서
            //시작할 수 있으면 그 강의실 바로 비어버려.
            if (종료시간힙.isNotEmpty() && 종료시간힙.peek() <= 시작시간) { // [S4][V2][V4]
                종료시간힙.poll() // [S4][V2]
            }

            // [S5] 현재 수업 종료시간 등록
            종료시간힙.add(종료시간) // [S5][V2][V5]
        }

        // [S_END] 최소 강의실 수 반환
        return 종료시간힙.size
    }
}
/*
[1,3]
[2,4]
[3,5]
수업이 잇다고 하자.(시작시간순으로 정렬된 것)
처음에 수업을 하겟지. 그럼 힙에 3을 넣는거임. 그 의미는 강의실 1개가 사용중이고 종료시간 3이다.
그리고 다음 수업을 봤어. 시작시간이 2야. 그러면 종료시간 3이니까 못쓰니까 또 추가해야지.
4를 추가해. 그래서 힙상태 : [3,4]가 됨
그리고 [3,5]수업은 시작시간 3인데, 강의실 처음에 넣었던게 시간 3에 끝남. 그러니 3을 뺌.
그리고 5추가해야겠지.
힙상태 : [4,5] 가 됨.
* */