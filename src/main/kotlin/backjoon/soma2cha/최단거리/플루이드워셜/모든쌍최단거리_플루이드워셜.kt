// BOJ: https://www.acmicpc.net/problem/11404
/*
[문제 요약]
- 모든 도시 쌍 사이의 최단거리를 구한다.
- 도달 불가능한 경우는 0으로 반환한다.
*/

class Solution {

    /**
     * [핵심 아이디어]
     * - 플루이드워셜은 "경유 도시"를 1개씩 늘리며
     *   모든 (i,j) 쌍의 최단거리를 갱신한다.
     *
     * [데이터 구조]
     * - V0 도시수(Int): 도시 개수
     * - V1 버스목록(Array<IntArray>): [시작,도착,비용]
     * - V2 무한값(Long): 도달 불가 표현값
     * - V3 최단거리표(Array<LongArray>): 1-based 거리 행렬
     * - V4 경유도시(Int): 중간 경유 정점
     * - V5 출발도시(Int): 시작 정점
     * - V6 도착도시(Int): 도착 정점
     * - V7 시작도시(Int): 입력 간선 시작
     * - V8 종료도시(Int): 입력 간선 종료
     * - V9 비용(Int): 입력 간선 비용
     * - V10 결과행렬(Array<LongArray>): 반환용 0-based 거리 행렬
     */
    fun solution(도시개수: Int, 버스목록: Array<IntArray>): Array<LongArray> {
        // [S0] 상태 초기화
        val 도시수 = 도시개수 // [V0]
        val 버스데이터 = 버스목록 // [V1]
        val 무한값 = Long.MAX_VALUE / 4 // [V2]
        val 최단거리표 = Array(도시수 + 1) { LongArray(도시수 + 1) { 무한값 } } // [V3]

        for (도시 in 1..도시수) {
            최단거리표[도시][도시] = 0
        }

        // [S1] 입력 간선 반영
        for (버스정보 in 버스데이터) {
            val 시작도시 = 버스정보[0] // [V7]
            val 종료도시 = 버스정보[1] // [V8]
            val 비용 = 버스정보[2] // [V9]
            if (최단거리표[시작도시][종료도시] > 비용) {
                최단거리표[시작도시][종료도시] = 비용.toLong()
            }
        }

        // [S2] 파라미터 없는 플루이드워셜 본체
        fun 플루이드워셜실행() {
            for (경유도시 in 1..도시수) { // [V4]
                // [S3] 출발/도착 도시 쌍 순회
                for (출발도시 in 1..도시수) { // [V5]
                    for (도착도시 in 1..도시수) { // [V6]
                        if (최단거리표[출발도시][경유도시] == 무한값 ||
                            최단거리표[경유도시][도착도시] == 무한값
                        ) continue

                        // [S4] 핵심 갱신: 경유를 통한 거리 완화
                        val 경유거리 = 최단거리표[출발도시][경유도시] + 최단거리표[경유도시][도착도시]
                        if (경유거리 < 최단거리표[출발도시][도착도시]) {
                            최단거리표[출발도시][도착도시] = 경유거리
                        }
                    }
                }
            }
        }

        플루이드워셜실행()

        // [S5] 결과 행렬 변환
        val 결과행렬 = Array(도시수) { LongArray(도시수) } // [V10]
        for (출발도시 in 1..도시수) {
            for (도착도시 in 1..도시수) {
                결과행렬[출발도시 - 1][도착도시 - 1] =
                    if (최단거리표[출발도시][도착도시] == 무한값) 0 else 최단거리표[출발도시][도착도시]
            }
        }

        // [S_END] 결과 반환
        return 결과행렬
    }
}
