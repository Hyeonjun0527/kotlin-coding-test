// BOJ: https://www.acmicpc.net/problem/1753
import java.util.PriorityQueue

/*
[문제 요약]
- 시작 정점에서 모든 정점까지의 최단거리를 구한다.
- 도달 불가능한 정점은 -1로 반환한다.
*/

class Solution {

    /**
     * [핵심 아이디어]
     * - 가중치가 음수가 없으므로 다익스트라를 적용한다.
     * - 우선순위큐에서 현재 가장 짧은 누적거리를 가진 정점을 먼저 확정한다.
     *
     * [데이터 구조]
     * - V0 정점수(Int): 정점 개수
     * - V1 시작점(Int): 시작 정점 번호
     * - V2 인접목록(List<MutableList<간선>>): 방향 그래프
     * - V3 최단거리목록(IntArray): 시작점에서 각 정점까지의 최소거리
     * - V4 방문완료(BooleanArray): 최단거리 확정 여부
     * - V5 우선순위큐(PriorityQueue<상태>): 누적거리 최소 힙
     * - V6 현재정점(Int): 큐에서 꺼낸 정점
     * - V7 현재거리(Int): 큐에서 꺼낸 누적거리
     * - V8 다음정점(Int): 인접 정점
     * - V9 간선가중치(Int): 인접 간선 가중치
     * - V10 결과배열(IntArray): 1..n 정점 결과(-1 포함)
     *
     * [처리 순서]
     * <1> 인접목록/최단거리/방문배열 초기화
     * <2> 간선 입력으로 인접목록 구성
     * <3> 시작점 거리 0 설정 후 큐 삽입
     * <4> 다익스트라실행() 함수 실행
     * <5> 최단거리 배열을 결과 형식(-1 포함)으로 변환
     */
    fun solution(정점개수: Int, 시작정점: Int, 간선목록: Array<IntArray>): IntArray {
        data class 간선(val 도착정점: Int, val 가중치: Int)
        data class 상태(val 정점: Int, val 누적거리: Int)

        // [S0] 상태 초기화
        val 정점수 = 정점개수 // [V0]
        val 시작점 = 시작정점 // [V1]
        val 무한값 = Int.MAX_VALUE
        val 인접목록 = List(정점수 + 1) { mutableListOf<간선>() } // [V2]
        val 최단거리목록 = IntArray(정점수 + 1) { 무한값 } // [V3] 시작점에서 각 정점까지의 최소거리
        val 방문목록 = BooleanArray(정점수 + 1) // [V4]
        val 우선순위큐 = PriorityQueue<상태>(compareBy { it.누적거리 }) // [V5]

        // [S1] 간선 입력 반영
        for (간선정보 in 간선목록) {
            val 출발 = 간선정보[0]
            val 도착 = 간선정보[1]
            val 가중치 = 간선정보[2]
            인접목록[출발].add(간선(도착, 가중치))
        }

        // [S3] 파라미터 없는 다익스트라 실행 함수
        fun 다익스트라실행() {

            // [S2] 시작점 초기화
            최단거리목록[시작점] = 0
            우선순위큐.add(상태(시작점, 0))

            while (우선순위큐.isNotEmpty()) {
                val 현재상태 = 우선순위큐.poll()
                val 현재정점 = 현재상태.정점 // [V6]
                val 현재누적거리 = 현재상태.누적거리 // [V7]
                
                if (방문목록[현재정점]) continue
                // 우선순위큐 최소 힙 특성상 "처음 poll된 시점"이 이 정점의 최단거리 확정 시점이다.
                방문목록[현재정점] = true

                // [S4] 핵심 확장: 인접 정점 완화
                for (다음간선 in 인접목록[현재정점]) {
                    val 다음정점 = 다음간선.도착정점 // [V8]
                    val 간선가중치 = 다음간선.가중치 // [V9]
                    val 후보거리 = 현재누적거리 + 간선가중치

                    if (최단거리목록[다음정점] > 후보거리) {
                        최단거리목록[다음정점] = 후보거리
                        우선순위큐.add(상태(다음정점, 후보거리))
                    }
                }
            }
        }

        다익스트라실행()

        // [S5] 결과 변환
        val 결과배열 = IntArray(정점수) // [V10]
        for (정점 in 1..정점수) {
            결과배열[정점 - 1] = if (최단거리목록[정점] == 무한값) -1 else 최단거리목록[정점]
        }

        // [S_END] 결과 반환
        return 결과배열
    }
}
