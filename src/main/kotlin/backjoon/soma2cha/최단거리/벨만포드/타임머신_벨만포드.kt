// BOJ: https://www.acmicpc.net/problem/11657
/*
[문제 요약]
- 시작 정점에서 모든 정점까지의 최단거리를 구한다.
- 음수 사이클이 시작점에서 도달 가능하면 `[-1]`만 반환한다.
- 도달 불가능한 정점은 -1로 반환한다.
*/

class Solution {

    /**
     * [핵심 아이디어]
     * - 벨만포드는 간선 완화를 최대 N-1번 반복한다.
     * - 그 후 한 번 더 완화가 가능하면 음수 사이클이 존재한다.
     *
     * [데이터 구조]
     * - V0 정점수(Int): 정점 개수
     * - V1 시작점(Int): 시작 정점 번호
     * - V2 간선목록(Array<IntArray>): [출발,도착,가중치]
     * - V3 최단거리(LongArray): 시작점 기준 최단거리
     * - V4 무한값(Long): 도달 불가 표현값
     * - V5 반복횟수(Int): 완화 반복 카운트
     * - V6 시작정점(Int): 간선 시작
     * - V7 도착정점(Int): 간선 도착
     * - V8 가중치(Int): 간선 가중치
     * - V9 갱신발생(Boolean): 반복 중 갱신 여부
     * - V10 음수사이클존재(Boolean): 음수 사이클 감지 여부
     * - V11 결과배열(LongArray): 반환용 거리 배열
     */
    fun solution(정점개수: Int, 간선목록: Array<IntArray>, 시작정점: Int = 1): LongArray {
        // [S0] 상태 초기화
        val 정점수 = 정점개수 // [V0]
        val 시작점 = 시작정점 // [V1]
        val 간선데이터 = 간선목록 // [V2]
        val 무한값 = Long.MAX_VALUE / 4 // [V4]
        val 최단거리 = LongArray(정점수 + 1) { 무한값 } // [V3]
        최단거리[시작점] = 0

        // [S1] 파라미터 없는 벨만포드 본체
        fun 벨만포드실행() {
            for (반복횟수 in 1 until 정점수) { // [V5]
                var 갱신발생 = false // [V9]

                // [S2] 간선 순회
                for (간선 in 간선데이터) {
                    val 시작정점번호 = 간선[0] // [V6]
                    val 도착정점번호 = 간선[1] // [V7]
                    val 가중치 = 간선[2] // [V8]

                    // [S4] 핵심 분기: 완화(relax) 가능 여부
                    if (최단거리[시작정점번호] != 무한값 &&
                        최단거리[도착정점번호] > 최단거리[시작정점번호] + 가중치
                    ) {
                        최단거리[도착정점번호] = 최단거리[시작정점번호] + 가중치
                        갱신발생 = true
                    }
                }

                // [S3] 조기 종료 검사
                if (!갱신발생) break
            }
        }

        // [S4] 파라미터 없는 음수 사이클 검사
        fun 음수사이클검사(): Boolean {
            for (간선 in 간선데이터) {
                val 시작정점번호 = 간선[0] // [V6]
                val 도착정점번호 = 간선[1] // [V7]
                val 가중치 = 간선[2] // [V8]

                if (최단거리[시작정점번호] != 무한값 &&
                    최단거리[도착정점번호] > 최단거리[시작정점번호] + 가중치
                ) {
                    return true
                }
            }
            return false
        }

        벨만포드실행()
        val 음수사이클존재 = 음수사이클검사() // [V10]

        // [S5] 결과 변환
        if (음수사이클존재) return longArrayOf(-1)

        val 결과배열 = LongArray(정점수 - 1) // [V11]
        for (정점 in 2..정점수) {
            결과배열[정점 - 2] = if (최단거리[정점] == 무한값) -1 else 최단거리[정점]
        }

        // [S_END] 결과 반환
        return 결과배열
    }
}
